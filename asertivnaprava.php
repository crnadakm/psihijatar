<p>Svaka osoba ima mogućnost da ih koristi i zauzme se za sebe, njihova primjena je važna za očuvanje samopoštovanja i poštovanja drugih. Asertivna prava proizlaze iz samog postojanja čovjeka i nezavisna su od pola, roda, nacionalnosti, boje kože i religijskih uvjerenja.
</p>

<h3 id="par1">Imam pravo da osjećam i izrazim osjećanja</h3>
<p>Osjećanja su važna u nastanku ljudskih ponašanja i nije moguće ni poželjno da budu uvijek prijatna. Neprijatna osjećanja ljude ne čine gubitnikom ili slabićem već su dio reagovanja na događaje koji se dešavaju. 
Prestaću sebi da zabranjujem osjećanja, potiskujem ih ili skrivam pratеći poruke usvojene kroz odrastanje: „Moraš biti jak/a da te ne zgaze“, „Samo slabići plaču“ ili „Curice ne smiju da se svađaju“. Ako tugujem tako prebolijevam gubitak. Ako se plašim znači da doživljavam da sam ugrožen/а. Ako sam nezadovoljan/na, ljut/a ili mi je dosadno, takvo osjećanje iskoristiću kao motivaciju da uradim nešto povodom stvari u vezi kojih se tako osjećam. <br>
Sebi ću dopustiti i prijatna i neprijatna osjećanja jer su i jedna i druga prirodna i predstavljaju sastavni dio zdravog postojanja. Imam pravo da budem tužna/an, srećna/an, uplašen/a, radostan/sna, razočaran/a ili ljut/a i imam pravo da to izrazim jer tek ako osjećanje izrazim, drugi mogu biti upoznati sa mojim unutrašnjim stanjem i otvaram prostor za uzajamno razumijevanje i rješavanje problema u odnosima.</p>

<h3 id="par2">Imam pravo da prosuđujem o sebi i svom ponašanju</h3>
<p>Prestaću da odbijam postavljanje ličnih granica i sopstvenu samostalnost tako što ću prestati da vrednujem sebe na osnovu onoga što drugi kažu o meni i tako što ću prestati da se prepuštam tuđim odlukama, procjenama i, često, zabludama. <br>
Koristeći ovo pravo prestaću da nastojim da se dopadnem svima izbjegavajući najmanji znak odbacivanja ili neprihvatanja. Prestaću da životnu snagu trošim kako bih svoje ponašanje prilagodio/la drugima i tako zadobio/la pažnju, „naklonost“ ili „ljubav“.
Prihvatajući odgovornost za ono što mislim, govorim i činim odbijam da budem pasivno kreiran/a od drugih ili „loš/a“ i „dužna/an“ ako nisam po tuđoj mjeri savršenosti. <br>
Birajući pravo da o sebi i svojim postupcima prosuđujem njegovaću stav samoprihvatanja kojim povećavam otpornost na ono što pretpostavljam da drugi o meni misle.</p>

<h3 id="par3">Imam pravo da se predomislim</h3>
<p>Sticanje novih iskustava i učenje vodi ka promjeni razmišljanja u vezi sa nečim. Promjena mišljenja nije pokazatelj moje neispravnosti, već nužnost u ličnom razvoju i kad promijenim sopstveno mišljenje o nečemu to, jednostavno, potvrđuje da umijem da mislim. 
Prestaću da se plašim da će me drugi procijeniti kao nedosljednu, prevrtljivu osobu slabog karaktera i ublažiti pa zanemariti svoj otpor ili krutost da promijenim mišljenje, stav ili uvjerenja jer hoću da se razvijam i napredujem i nužno je da budem spremna/an na promjene.</p>

<h3 id="par4">Imam pravo da zatražim ono što želim</h3>
<p>Koristiću ovo pravo da bih poboljšao/la svoje odnose sa prijateljima ili porodicom, u braku, vezi, na poslu ili u školi i izbjegao/la probleme u komunikaciji jer se ne podrazumijeva da onaj drugi zna šta je potrebno da učini ili kaže.
Prestajem da vjerujem da će drugi „čitati misli“, sami znati šta želim i saopštavaću to drugima na njima razumljiv način.
<h3 id="par5">Imam pravo da ne razumijem</h3>
Kao što drugi mogu saznati kako se osjećam ili šta želim ukoliko im to saopštim, tako i ja mogu razumjeti njihov unutrašnji svijet ukoliko usvojim pravo da ne razumijem i postavljam pitanja u vezi stvari koje su mi nejasne. <br>
Prestaću vjerovati da umijem čitati tuđe misli i unaprijed prepoznajem tuđe želje ili potrebe i predvidim tuđa očekivanja i prestaću da smatram da sam „loš/a“ ili doživljavam se „prozvanim/om“ ukoliko je druga osoba bijesna, uvrijeđena ili uznemirena.
Koristeći i braneći svoje pravo da ne razumijem, pozivaću druge u razgovor koji je mogućnost da dođemo do boljeg razumijevanja. </p>

<h3 id="par6">Imam pravo da ne znam</h3>
<p>Prihvatiću da sam ljudsko biće i s tim, kao i bilo ko drugi, biološki ograničen/a da pohranim sva znanja i prihvatiću da nisam u mogućnosti da predvidim sve moguće posljedice svojih budućih aktivnosti. <br>
Zadržaću spremnost da se razvijam kroz prihvatanje sopstvenog prava na neznanje i otvorenost za nove spoznaje.
</p>

<h3 id="par7">Imam pravo da kažem : „NE!“</h3>
<p>Koristeći ovo pravo sebe ću spriječiti da nekome pomažem kada to ne želim i dopustiti sebi da postavim granicu. 
Odbijajući da pomažem osobi da riješi neki problem za koji je odgovorna i u objektivnoj mogućnosti da ga sama riješi odbijam da tuđi problem bude i moj. Prepuštanjem odgovornosti drugima omogućavam i njima da razviju sposobnost za rješavanje problema. </p>

<h3 id="par8">Imam pravo da pogriješim</h3>
<p>Greška u postupcima ili ponašanju nas ne čini pogrešnim ljudskim bićima. Prestaću da oduzimam sebi pravo na grešku u ponašanju jer ne postajem ja kao ličnost pogrešan jer sam nešto uradio pogrešno. <br>
Prestaću da izbjegavam aktivnosti za koje mislim da mogu u njima pogriješiti i da ne priznajem greške koje primijetim u svom ponašanju birajući često da okrivim druge za njih, jer oduzimanjem sebi prava na grešku otežavam si aktivnosti u kojima želim biti ostvaren/a. <br>
Odustaću od sklonosti da odlažem obaveze i aktivnosti, i da „greške“ doživljavam kao nešto katastrofično i ugrožavajuće po samopoštovanje. Koristeći pravo da pogriješim otkloniću nastojanje da savršeno uradim stvari i otkloniću zaludno podsvjesno nastojanje da ću kao „ličnost ostati savršen/a“, navikavajući se na to da sam i kad nešto pogrešno učinim kao čovjek sasvim u redu.</p>

<h3 id="par9">Imam pravo da se ne opravdavam </h3>
<p>Kada koristim ovo pravo onda ne nastojim da se drugim ljudima bezuslovno dopadnem, prestajem da se često izvinjavam i pravdam ako radim ili pomislim nešto što drugi ne podržava, povremeno i u gotovo beznačajnim situacijama, npr. prodavcu čiji proizvod nisam kupio/la. <br>
Kada koristim ovo pravo onda ne izmišljam gomilu „dobrih razloga“ kada postupim prema svojim željama a ne prema tuđim očekivanjima. Stalno ugađanje drugima obično ima suprotan efekat; drugi ljudi me manje poštuju i manje im se sviđam. Kada biram da se ne opravdavam onda ne izgleda kao da se izvinjavam zbog onog što jesam.</p>

<h3 id="par10">Imam pravo na sopstvene izbore </h3>
<p>Sebi neću oduzeti ovo pravo pa odgovornost za to kako se osjećam ili ponašam prebaciti na nekog drugog čovjeka, okolnosti ili višu silu i okriviti ih ako nisam zadovoljan. <br>
Neću sebi oduzeti ovo pravo zbog pridavanja značaja spoljašnjim faktorima. <br>
Prestaću da vjerujem u vaspitne predrasude ili socijalna ograničavanja : „Nisi u stanju da donosiš odluke zato što si dijete“, „Nisi još dovoljno kompetentan/tna/ iskusan/sna“ ili „Star/a si, tvoje vrijeme je prošlo“ i prestati da pristajem na to da drugi misle i odlučuju umjesto mene i tako ostanem naizgled sigurniji/a od ostavljanja, odbacivanja, neuspjeha, kritike ili lijepljenja “etiketa”.<br>
Prestaću da se plašim i preuzeću odgovornost za svoja osjećanja, mišljenje i ponašanje.</p>

