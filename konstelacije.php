<p><b>Sistemske (porodične) konstelacije</b> su pomoćna psihoterapijska metoda koja se oslanja na procesiranje negovornog tjelesnog i emotivnog doživljaja u sadašnjem trenutku. Koriste se za olakšavanje razumijevanja kako se prenose povređujuća bolna iskustava u međugeneracijskom sistemu, kako nerazriješeni sukob i osjećanja utiču na naše zdravlje i stabilnost. Sistemske konstelacije predlažu moguća razrješenja i pomažu u liječenju simptoma psihičke neuravnoteženosti (neurotičnost, depresivnost, zavisnost, elementi psihotičnosti...) pružajući dodatni aspekt shvatanja različitih oblika manjkavo prilagođenog ponašanja, tegobnih osjećanja kao i mišljenja koja podržavaju patnju. Pored toga ukazuju na mogućnost drugačijeg sagledavanja i doživljavanja tjelesne bolesti, tjelesnog hendikepa, posljedica nasilja i ratnih zločina, neplodnosti, starateljstva/usvajanja djece, problema u partnerskim odnosima. </p>
<p>Metoda je prepoznata i osmišljena po iskustvu doživljaja zajedničke zbirne svijesti porodičnih sistema. Zbirna svijest teži da u sistemu podržava zakonitosti. Prihvatanjem ovih zakonitosti u okviru sistema omogućava se održavanje uređenosti sistema. Odstupanja od dovoljno dobre uređenosti sistema se događaju na mnogo različitih načina - isključivanjem člana porodice (ili člana drugog sistema), zaboravljanjam onih koji su umrli mladi, izbjegavanjem pominjanja i sjećanja na vanbračnu djecu, djecu datu na usvojenje, prekinute trudnoće, otpisivanjem važnosti značajnih ranijih partnerskih veza ili istovremenih partnerskih veza kao i drugih dijelova stvarnosti o kojima se ne govori. Posljedice tog sklanjanja od stvarnosti mogu osjećati sami pojedinci ali i osobe iz mlađih generacija.</p>
<p>Simptomi različitih kvaliteta (depresija, zavisnost, mentalne i fizičke bolesti, neplodnost, teškoće u zasnivanju dugotrajne intimne veze...) mogu postojati, a da nema svjesnosti osobe o tome zašto se tako osjeća. Porodični sistem je pored toga pod dubokim uticajem rata, migracija, kulturnih razlika pa je takve i slične faktore korisno uvažiti unutar istraživanja sistemske (porodične) konstelacije.</p>
<br/>
<p>Porodične (sistemske) konstelacije se izvode (i nude) u okvirima grupnog terapijskog susretanja. Radionice su sa 15–30 osoba/klijenata koje traju više dana. Obično sudionici grupe sjede uz ivice prostorije, tako da je u sredini slobodan prostor za rad.</p>
<br/>
<p>Učesnici se dobrovoljno javljaju za postavljanje svoje konstelacije, koje se mogu odnositi na njihove sadašnje porodice ili na porodice iz kojih potiču. Teme rada mogu biti povezane i uz poslovno okruženje kao i uz druge oblike društevnih grupa u kojima se klijent kreće. Terapeut (voditelj konstelacije) razgovara sa klijentom o tome ko pripada (ili je pripadao) porodičnom (ili drugom) sistemu, koji su značajni događaji, da li je neko rano umro, jesu li roditelji imali ranije partnere i djecu iz ranijih veza… a potom vodi razgovor ka odabiru ličnog pitanja na koje klijent očekuje odgovor. Klijent zatim izabere među sudionicima osobe koje će predstavljati stvarne članove njegove porodice kao i predstavnika za sebe (obično se za predstavnika biraju osobe istog pola kao i stvarni član porodice ali nije nužno). Koristeći prostor za rad klijent povede, usmjerava i postavi predstavnika na poziciju koju doživljava kao “njegovo mjesto“. Razmještaj kojim se počinje konstelacija odražava, dakle, klijentov doživljaj mjesta i odnosa članova njegove porodice/sistema. Predstavnici članova sistema/porodice nakon što su postavljeni daju povratnu informaciju. Opisuju svoj doživljaj “ovdje i sad”, u odnosu na druge predstavnike, osjećaju li se dobro ili loše na mjestu gdje jesu, da li im je toplo ili hladno, neugodno, tužno, fizički bolno…. Ako su povratne informacije ukazale da nedostaje još neko važan nova osoba se može zamoliti da stane kao predstavnik. U nastavku procesa terapeut korak po korak repozicionira predstavnike. Proces je aktivan, može uključivati promjene u grupisanju, poretku predstavnika, smjeru u kom su okrenuti, stavu ili međusobnoj udaljenosti. Predstavnici daju povratne informacije o promjenama koje doživljavaju sve dok ne se nađe rješenje u kojem uobičajena početna napetost nestaje. Nakon pronalaska dobrog (“pravog”) mjesta za svakog pojedinog člana porodice terapeut može upitati klijenta da stane na mjesto “njegova” predstavnika. U zavisnosti od situacije mogu se koristiti afirmativne izjave, kratke rečenice kojima se potvrđuje novi položaj člana porodice ili terapijske geste raznih oblika (zagrljajaj, poklon, pozdrav). Moguće je da se pojave snažne emocije među predstavnicima u konstelaciji, posebno kod klijenta čija tema je postavljena. Terapeut ohrabruje klijenta da sebi dozvoli osjetiti i doživjeti svoje promijenjeno mjesto unutar porodičnog sistema. Tumačenja iza rada nisu uobičajena. Postavke su često otvorenog završetka i korisno je pustiti da se osoba navikne na novu mogućnost za šta je potrebno neko vrijeme. Radionice mogu biti usmjerene na pojedine teme npr. psihosomatske bolesti ili biti dio postojeće terapije, u kojem slučaju i klijent i terapeut prisustvuju radionici. Prvobitno se izvodi jedna terapijska seansa, koja se može ponoviti nakon određenog perioda.</p>
<p>Tehnika porodičnih konstelacija se može primijeniti i kao individualna terapija a tada zamjenski simboli npr. listovi obilježenog papira ili figure simbolizuju članove porodice. Preventivna upotreba porodičnih konstelacija korisna je osobama kad ulaze u novo razdoblje svog života (roditeljstvo, novi posao...) a terapijski principi iz ovog modela su koriste I unutar konteksta poslovnog savjetovanja organizacija (poslovnih sistema).</p>
