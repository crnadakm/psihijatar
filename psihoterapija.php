<h3 id="par1">Kome je psihoterapija korisna?</h3><br>
<p>Psihoterapija može biti korisna svakoj osobi koja smatra da joj je potrebna takva vrsta podrške ili osobi kojoj drugi preporuče takvu mogućnost jer znaju da već dugo pokušava da izađe na kraj sa nekim   problemima a ne uspjeva ili su primijetili emocionalno doživljavanje/ponašanje koje odstupa od uobičajenog za osobu (ovo ne mora biti dovoljan razlog da se započene terapija ali može biti koristan podstrek da osoba to razmotri).</p><br><br>

<h3 id="par2">Šta je psihoterapija?</h3><br>
<p>Psihoterapija je skup metoda, procedura i tehnika kojima je cilj liječenje psihičkih tegoba razgovorom. Razgovor se vodi između terapeuta i jednog ili više sagovornika/klijenata. Namjena  ovakvog razgovora je olakšavanje psihičkih tegoba, sagledavanje mogućih rješenja problema ili razvijanje ličnosti čime se poboljšava kvalitet života osobe.</p>
<p>Značajan broj onih koji zatraže psihoterapiju su zdrave, funkcionalne osobe koje ne očekuju liječenje nego stručnu podršku za lični rast i razvoj otkrivanjem novih potencijala i uvida o sebi i drugima. Psihoterapija može pomoći i kada osoba shvati da nešto u njenom životu nije kao što želi da jeste i nastoji da nešto bude drugačije ili kada je nezadovoljna svojim životom ili odnosima, ali nije sigurna zbog čega. Ipak najčešće su razlozi za početak terapije želja za olakšavanjem trenutnih funkcionalnih životnih poteškoća (poboljšanjem upravljanja stresom, povećanjem motivacije,...) ili potreba za liječenjem kompleksnih psihičkih problema (fobije, depresija, paničnost, nesanica...). </p>
<br><br>

<h3 id="par3">Kako "radi" psihoterapija?</h3><br>
<p>Dijelovi od kojih se sastoji psihoterapijski proces su mogućnost da se govori o sebi bez straha od osuđivanja, učenje da se prepoznaju, vrednuju i opišu vlastita osjećanja, sagledavanje životnih događaja iz drugačijih uglova, razmatranje manje oštrih načina na koje procjenjujemo sebe i svijet oko nas kao i empatičan i prihvatajući odnos terapeuta prema osobi.</p>
<p>Promjene koje osoba čini kao rezultat procesa, vode ka spremnosti da poteškoće riješi samostalno i   sličnu sposobnost zadrži u budućnosti.</p>
<p>Terapeutski autoritet od koga se uči šta treba da se radi ili vještina terapeuta da postavlja pitanja koja osoba ne postavlja sama sebi nisu objašnjenje kako  psihoterapija „radi“. Iako mnogi vjeruju da bi ključni način “rada” u psihoterapiji mogao da se objasni “ventilacijom” (osoba govori a neko je sluša, prihvata i ne osuđuje pa stoga dođe do olakšanja) ili “placebo efektom” (uvjerenje osobe da će psihoterapija pomoći a u stvari nema djelovanja) to takodje nije potpuno objašnjenje psihoterapije. Sve ovo pomaže i koristi se u psihoterapiji, ali nije osnova izliječenja</p>
<p>Zajedničko za različite psihoterapijske pravce koji koriste drugačije tehnike i filozofiju  o tome šta ljudima pomaže jeste odnos (alijansa) koji se uspostavlja sa terapeutom. <br>
Prikladno uspostavljen psihoterapijski odnos je "najljekovitiji" faktor psihoterapije i ključ njenog uspjeha. Takav odnos nije odnos dvoje saradnika, ni dvoje drugara, ni odnos učitelja i učenika, tkđ. nije ni onakav kako izgleda uobičajeni odnos ljekara i pacijenta. 
Odnos je profesionalan i određen smjernicama (terapeut poštuje pravila struke i etička načela, osoba terapeutu plaća za uslugu), ali s vremenom postaje i blizak pa sa uspostavom povjerenja, često osoba bez straha od odbacivanja i osude govori i o stvarima koje  nisu nikom rečene ili pokazuju osjećanja koja nisu ranije bila vidljiva drugima. Terapeut nastoji da upozna i razumije osobu te način na koji doživljava svijet bez obzira koliko je različit od njegovog sopstvenog, zatim kako osoba rješava probleme ili prilazi ljudima, uz povremeno postavljanje pitanja koja mogu biti teška ili sučeljavanje osobe sa njenim iskrivljenim uvjerenjima ali bez osude. <br>
Sa psihoterapeutom osoba ne mora biti saglasna, biti ljuta ili prema njemu ispoljiti različite emocije bez bojazni da će ga one povrijediti. Terapeut događaje u profesionalnom odnosu ne shvata lično i sve što klijent ispolji terapeut koristi da bolje razumije i na najbolji način podrži traženje rješenja. <br>
Prijatelji, rođaci i kolege ne mogu biti osobi psihoterapeuti jer s njima postoje već izgrađeni lični odnosi te se terapijska alijansa na ovakav način ne može uspostaviti.</p>

<br><br>

<h3 id="par4">Koliko traje psihoterapija?</h3><br>
<p>Terapija traje od nekoliko mjeseci do nekoliko godina što zavisi od brzine i tempa terapijskog rada, postavljenih ciljeva, težine problema, izvježbanosti u  samoposmatranju i refleksivnosti, ličnom kapacitetu za uvid itd. </p>
<p>Terapeutske tehnike se dosta razlikuju jedne od drugih (verbalni materijal, asocijacije, rad sa tijelom, umjetničko izražavanje, igranje uloga itd.) iako postoje i zajedničke  stvari koje su nezavisne od modaliteta.</p>
<p>Bihejvioralna terapija je uglavnom usmjerena na  konkretno ponašanje koje se nastoji promijeniti, ima jasno definisan uži cilj i obično traje kratko, 10-20 seansi, ponekad i samo nekoliko seansi.</p>
<p>Psihoterapija u nekom od humanističkih modaliteta (transakciona analiza, geštalt, psihodrama) kojima se pobuđuju potencijali koje osoba posjeduje a nepotpuno koristi, traje od šest mjeseci do dvije godine, iako može trajati  duže ako se terapeut i klijent usaglase oko toga.</p>
<p>"Dubinski" pravci (psihoanaliza) traju najduže pošto imaju najšire postavljene ciljeve i usmjereni su na analize nesvjesnog materijala do koga se dolazi vrlo polako, te obično traju nekoliko godina.</p>



