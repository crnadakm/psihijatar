				<div class="col-md-12 tekst">
				<p>Neki ljudi prožive cijeli život želeći da sebe dožive kao “normalnu” osobu i čini im se nedostižnom ta (prividna) sposobnost drugih ljudi da žive svoje živote bez napora.</p>
				<p>Ranije sam se pitao o sposobnosti da budem “normalan”, tokom svoje psihoterapije tražio sam pomoć da se osjećam onako kako sam mislio da se drugi ljudi “normalno” osjećaju. A sada kada sam terapeut, klijenti traže isto od mene, u svakodnevnom radu slušam rečenicu “Samo želim da budem normalan/na!”. </p>
<br/>
				<p>Ovaj unutrašnji doživljaj “nenormalnosti” je prožimajući, slično kao krivica ili stid i veže se za gotovo svaku životnu situaciju. Takva osoba se iz negativne životne pozicije bavi idejom o različitosti od drugih pa se mnogi događaji, čak i banalni susreti sa nepristojnim vozačem autobusa ili sa provokatorom u kafani, doživljavaju kao pokazatelji da nije “normalna” i da je gubitnik. </p>
<br/>
				<p>“Osjećam se skroz drugačijim od drugih ljudi” je izjava koja je često pokazatelj depresivnosti. Depresivnost se usvoji kao sastavni dio postojanja i neki obični trenuci života postaju nekako teški i prevažni i u njima osoba traži simboličke dokaze o tome kako je “drugačija”. Kad bi se ova sposobnost posmatranja života kroz simbole pretočila u umjetnost to bi postala komunikacija koja ozdravljuje ali češće sebe osoba zarobi u sivim dijelovima uma iako bi bilo vjerovatno bilo zdravije ne baviti se time u svakodnevici i postojati bez dokazivanja vlastite pogrešnosti. Iza ovoga slijedi “liječenje” alkoholom, drogom ili povlačenje ili prekomjeran rad ili samopovređivanje …</p>
<br/>
				<p>Pitanje samom sebi “kako da postojim na prihvatljiv način” je moglo početi tako što je neko bio “onaj manje vrijedan” u domu iz kojeg potiče. Kasnije, u školskom periodu, može se već primijetiti koje je dijete emocionalno izolovano jer teško je naučiti samoprihvatanje kada vas neprihvatljivosti uče od malih nogu. Doživljaj izdvojenosti postaje naglašen, veza sa drugima krhka pa dijete ne umije da se doživi kao povezano s drugima i stvarni dio zajednice pa onda osmisli šablon "ja protiv svih ostalih (koji su popularni, koji su uspješni, koji su voljeni)". “Normalnost” postaje proganjajući unutrašnji glas kojim se dijete poredi sa drugima koji su naizgled savršeni objekti uz nastojanja da se vezuje za druge uglavnom na toksične načine. Ovo ne prestaje sa odrastanjem.</p>
<br/>
				<p>Vjerovanje da smo drugačiji na pogrešan način sadrži i dio o dvojnosti introvertno/ekstrovertno. Mnogi ljudi doživljavaju sebe kao introverte koji se muče u ekstrovertnom svijetu. Biti “ekstrovert” podrazumijeva društvenost, mnoštvo socijalnih aktivnosti i “razonoda” (nerijetko prisilnih) kao i uviđanje da su ekstroverti često prepoznati kao vredniji. Teško je prestati misliti da ste nenormalni kada ste na „pogrešnoj“ strani te dvojnosti. </p>
<br/>
				<p>Ali šta je tako dobro u normalnom? Svakako, to može značiti pripadnost i prihvatanje od strane grupe. Jedan od načina da uđete u većinsku grupu je da se pravite da ste drugačiji nego što jeste (obrazovani ili bogati ili heteroseksualni ili...), takodje je moguće promijeniti način oblačenja ili šišanja ali šta ako je osoba različita prema npr. tjelesnom izgledu (npr. boja kože, veličina ili invaliditet) , to nije lako promjeniti i uklopiti se u mjerila neke od grupa pa osoba ostaje u manjini i različita. Nažalost, čak i unutar manjinskih grupa, “standard normalnosti“ često se primjenjuje, pa se oponaša obrazac većinskog društva i teži se izdvajanju onih koji su manje ili više “normalni” prema očekivanjima većine u manjinskoj grupi. </p>
				<p><i>Ako uzmete grupu plavokosih, plavookih, osamnaestogodišnjih mladića, iste težine i visine, iz istog grada, sa očevima doktorima i fakultetski obrazovanim majkama, sa jednom mlađom sestrom, i stavite ih u istu sobu, za pola sata bi se formirala hijerarhija. Teško da postoji neka kultura, etnička pripadnost, seksualna orijentacija, ponašanje ili politička grupa u kojoj se ovo raslojavanje ne bi dogodilo.</i></p>
				<p>Ovakva prividna poželjnost “normalnog” - tog skoro nemogućeg “uzvišenog” stanja, ipak nije opjevana ili oslikana u umjetnosti, nema ulica koje nose imena u slavu normalnosti. Psihologija ne piše knjige o ovakvim “normalnim osobama”, nema ih u glavnim kartama tarota, pretpostavljam da ne postoji pozorišna drama ili opera u kojoj kao vrhunac djela glavni lik dostigne stanje normalnog. Čini se da postoji jaz između stremljenja normalnosti kao vanjskog stanja i njene poželjnosti kao unutrašnjeg stanja (prije je za vjerovati da je mnogima, riječ normalno sinonim za dosadno i izgovora se s podsmijehom). </p>
				<p>Kada na terapijskom susretu počnem da se raspitujem šta je “normalno”, često otkrijem da to nije samo želja da se bude srećan, siguran i da se bude predani borac za svoju sudbinu. Biti normalan, često zapravo znači posjedovati kuću i/ili biti u dugoj vezi, “imati” djecu i voziti solidan automobil (uglavnom na kredit), ljetovati na moru deset dana i zimi otići na skijanje a jednom u dva mjeseca večerati u restoranu sa prijateljima. To što mnogi ljudi ne žive ovako se ne uvažava kao dovoljan argument da je u redu biti “normalan” i na drugačiji način. </p>
				<p><i>Mediji su u potpunosti vođeni tržišnom vrijednosti pa se takvi standardi primjenjuju i na ljude. Društva kakvo je naše imaju opsesiju imovinom, pokretnom i nepokretnom, predstavlja se kao da se sve može kupiti i da je sve dostižno samo ako se dovoljno potrudiš, kao da je posjedovanje nekih papira i podizanje velikog kredita znak vjerodostojnog moralnog karaktera. Sposobnost korištenja godišnjeg odmora na reklamiranom dijelu svijeta ili veza para, po mogućnosti heteroseksualnog i koji uglavnom imaju seksualni odnos u misionarskoj pozi, ili uklanjanje dlaka sa tijela na tačno određeni način ili uzimanje hrane koja se sprema baš ovako slušanje muzike koja se najviše pušta na kanalima koji donose profit, gledanje sportskih takmičenja uz pivo ili slatki napitak….podrazumijeva se kao normalno.</i></p>
<br/>
				<p>Pozivanja mnogih terapeuta na propitivanje gornjeg “definisanja” normalnosti nisu dovoljna jer je uticaj ogromne moći ljudskog neznanja o normalnosti veoma snažan. </p>
				<p>Zaboravlja se lako da morate biti prihvaćeni od strane drugog da biste bili u partnerskoj vezi, zaboravlja se da biste posjedovali nekretninu često morate biti odabrani za dobro plaćen posao. Zanemaruje se da veza bez razumijevanja, bez prihvatanja, bez seksa ili prepuna mutnih tokova i zlostavljanja može da osobu guši. Skreće se pogled sa toga da ponekad kuća može biti vlažna i bučna, a praktično jedva u posjedu jer se u stvari skriva istina o hipotekarnom kreditu. Ali kvačica na dijelu društvene prihvatljivosti je zakačena pa “Nema veze“. </p>
<br/>
				<p>Dokle ovako? Normalno počinje izgledati kao odsustvo osjećaja – ne utrnulost, već potpuni nedostatak čežnje i nedostatak stida. “Divna“ nesvesnost, neznanje - biti srećan bez mentalnog delanja; živjeti kao egzistencijalna visibaba u polju jer čini se da samo “normalni” rade svoje poslove, pronalaze veze i stiču kuće bez vidljive borbe ili samopreispitivanja. </p>
<br/>
				<p>Ipak naglasiću da biti “normalan” je veoma prikladno i ne sadrži nikakvu slabost samo po sebi. U redu je misliti slično sa mnogima iz svoje okoline, biti okrenutim vrijednostima koje većina cijeni, držati se u grupi sa onima koji su slično postavili život. Problem je ukoliko se, iz ove pozicije, pravo da budu onakvi kakvi su osporava onima koji su drugačiji od ovoga. </p>
<br/>
				<p>Dakle, kako biti od pomoći “nenormalnim” klijentima da dožive svijet sa većim osjećajem sigurnosti i više blagosti prema sebi? Kao terapeut, oprezan sam u ponudi rješenja jer jedan način ne odgovara svakom. Mogu biti sa svojim klijentima i podržati ih u tome da se okruže ljudima koji su im sličniji, da pogledaju svijet kao mjesto gdje nisu sami i traže mjesto prihvatanja sebe i života preživljavanjem izazova i ličnim razvojem, kroz vrijeme će se otvarati mogućnost prepoznavanja da različitosti od drugih nisu takve kako se na početku činilo. </p>

