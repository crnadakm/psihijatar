				<h4>Šta je „transakciona analiza“?</h4>

				<p>Transakciona analiza (TA) je teorija ličnosti i sistemska psihoterapija čiji je cilj lični razvoj i promjena. Osnivač ovog psihoterapijskog pravca je Erik Bern koji je 1958. godine objavio rad  koji promoviše novi psihoterapijski metod koji naziva Transakciona analiza. TA se može koristiti u svakoj oblasti u kojoj postoji potreba za razmijevanjem pojedinaca, njihovih odnosa i komunikacije. TA sadrži elemenete psihoanalitičkog, kognitivnog i humanističkog pristupa.</p>


				<h4>TA kao teorija ličnosti</h4>

				<p>Kao teorija ličnosti, TA nudi sliku o tome kako su ljudi psihološki strukturisani koristeći tročlani model, poznat kao <b>model ego stanja</b>. Taj model nam omogućava da razumijemo kako ljudi funkcionišu i kako izražavaju svoju ličnost kroz ponašanje. Ovaj model opisuje tri ego stanja: ego stanje <b>Odrasli</b>, ego stanje <b>Roditelj</b>  i ego stanje <b>Dijete</b>. Ego stanje pretstavlja skup povezanih ponašanja, mišljenja i osjećanja. U ego stanju Odraslog smo onda kada koristimo sve resurse koji nam kao odrasloj osobi stoje na raspolaganju; tj kada se sada i ovdje osjećamo, ponašamo i mislimo tako da to predstavlja direktnu reakciju na događaje oko nas.  Kada se ponašamo, razmišljamo i osjećamo tako da kopiramo roditelje ili roditeljske figure, kažemo da smo u ego stanju Roditelja. Kada se ponašamo, osjećamo i mislimo kao kada smo bili djeca, kaže se da smo u ego stanju Djeteta.</p>


				<h4>Teorija komunikacije</h4>

				<p>TA nas upoznaje i sa teorijom komunikacije koja može poslužiti kao osnova za analizu sistema i organizacija. Kada dvije osobe komuniciraju, obje mogu jedna drugoj da se obrate iz bilo kojeg od svoja tri ego stanja. Ova razmjena komunikacije se naziva <b>transakcija</b>. Transakcija se dešava kada vam ja ponudim neku vrstu komunikacije, a vi mi odgovorite. Početna komunikacija se naziva stimulus, a uzvratna odgovor. Komunikacija između ljudi uvijek se odvija u obliku niza transakcija. Kod analize transakcija koristimo model ego stanja i pomoću njega objašnjavamo šta se dešava u toku procesa komunikacije.</p>
				<p>Ideja da svako od nas može da bira način na koji će da uputi transakciju se naziva <b>Transakcioni izbor</b> i on je od pomoći kada neko želi da prekine poznate, neprijatne razmjene sa drugima.</p>
				<p>Kada dvije osobe razmjenjuju transakcije, jedna od njih šalje signal da je drugu prepoznala, a druga osoba uzvraća prepoznavanje. Jedinica prepoznavanja u TA se naziva <b>strouk</b>. Ljudima su potrebi stroukovi za fizičko i psihičko blagostanje.</p>
				<p>Kada ljudi razmjenjuju transakcije u grupama ili parovima, oni koriste vrijeme na razne specifične načine koji se mogu prikazati i analizirati. To se zove <b>analiza strukturiranja vremena</b>. Erik Bern je naveo 6 načina strukturiranja vreman: Povlačenja, Rituali, Razonode, Rad ili aktivnost, Igre i Intimnost ili bliskost.</p>
					

				<h4>Životni skript</h4>

				<p>TA nam takođe nudi teoriju razvoja djeteta. Koncept <b>životnog skripta (scenarija)</b> govori o tome kako naši sadašnji obrasci ponašanja vode porijeklo iz djetinjstva. Moguće je da nastavimo ponavljati strategije koje smo razvili u djetinjstvu i kada odrastemo, čak i kada one kod nas izazivaju osjećaj neuspjeha i patnje. Samim tim, TA nam pruža i teoriju psihopatologije.</p>
				<p>Svako do nas napiše za sebe svoju životnu priču još  djetinjstvu. Ta priča ima početak, sredinu i kraj. Osnovni zaplet napišemo kada smo toliko mali da znamo tek nekoliko riječi. Kasnije u djetinjstvu dodajemo još detalja, ali većina toga se napiše do sedme godine. Priču popravljamo u toku adolescencije. Kao odrasle osobe, uglavnom više nismo svjesni životne priče koju smo za sebe napisali, iako je možda dosljedno živimo. Velika je vjerovatnoća da smo svoj život organizovali tako da idemo prema poslednjoj sceni koju smo odredili dok smo bili mali. U psihoterapijskoj primjeni se koncept životnog scenarija koristi kako bi razumjeli na koji način ljudi mogu sami sebi nesvjesno da prave probleme, i kako se treba postaviti da bismo te probleme riješili.</p>
				<p>Kako bi pojačali svoj životni skript, ljudi česti igraju <b>Igre</b>. Skript je opšti obrazac koji se primenjuje na životne izbore i životni tok pojedinca, a igre su načini na koje osoba neka svoja skriptna uverenja izražava u komunikaciji s drugim ljudima. Kroz igre osoba konstruiše socijalnu stvarnost kako bi je uklopila u svoj skriptni referentni okvir. O Igri govorimo onda kada primećujemo da se neko sistematično ponaša prema drugim ljudima prema obrascu koji sadrži neočekivani obrt. U pitanju je dvofazni obrazac prema kojem nakon prve faze sledi nagli obrt i druga faza komunikacije. Psihološke igre se igraju nesvjesno, uvijek dovode do naglog obrta i rezultat je osjećanje zbunjenosti i neshvaćenosti. Igra je serija komplementarnih transakcija koje se približavaju vrlo određenom, predvidljivom ishodu. Ako ste se nakon interakcije sa drugom osobom, gdje ste se na kraju osjećali loše, pitali zašto vam se ovo „stalno“ dešava ili kao se ovo „opet“ desilo velika je šansa da ste igrali Igru.</p>


				<h4>TA u praktičnoj primjeni</h4>


				<p>U oblasti praktične primjene, TA nam nudi sistem psihoterapije koji može da se koristi u liječenju svih tipova psiholoških poremećaja, od svakodnevnih životnih problema do težih poremećaja. Može se primjenjivati u individualnoj, grupnoj, porodičnoj terapiji i terapiji parova.</p>
				<p>	Osim u terapeutske svrhe, TA se takođe koristi u obrazovnim ustanovama, gdje pomaže nastavnicima i učenicima da ostvare jasnu komunikaciju i izbjegnu neproduktivne konfrontacije.</p>
				<p>	TA se pokazala kao izuzetno moćna kod obuke o upravljanju i komunikacijama, kao i analizi organizacija.</p>


				<h4>Filozofske pretpostavke na kojima se TA temelji</h4>


				<p>Transakciona analiza se zasniva na određenim filozofskim pretpostavkama koje se odnose na ljude, život i ciljeve promjene. Tri osnove filozofske pretpostavke TA su:</p>
				<ul>
					<li><b>Ljudi su OK.</b> To znači da svi imamo vrijednost i dostojanstvo kao ljudi i da prihvatamo sebe takve kakvi smo. To je iskaz o biću, a ne o ponašanju. Prihvatamo nečije biće, iako možda ne prihvatamo njegovo ponašanje.</li>
					<li><b>Svako ima sposobnost da misli.</b> Svako može da odluči šta želi od života, ima odgovornost i prihvata posljedice svojih odluka.</li>
					<li><b>Ljudi odlučuju o svojoj sudbini, a te odluke se mogu mijenjati.</b> Kao djeca usvajamo strategije ponašanja koje su nam poslužile da dobijemo ono što želimo i te strategije nastavljamo da koristimo u odraslom dobu (čak i kada su posljedice tih strategije bolne po nas). Bitno je naglasiti da svaku odluku koju smo donijeli, kasnije možemo promjeniti. Ljudi se mogu promjeniti, a promjenu ostvarujemo ne samo uvidom u stare obrasce ponašanja, već i aktivnim odlučivanjem da te obrasce promjenimo.</li>
				</ul>


				<h4>Osnovni principi TA prakse</h4>
				<ul>
					<li>Sklapanje ugovora. Na početku terapijskog procesa sklapaju se „administrativni“ i „terapijski ugovor“. Administrativni ugovor je sporazum između transakcionog analitičara, tj. terapeuta i klijenta o detaljima plaćanja, trajanju tretmana i ostalim administratitvnim stvarima važnim za njihov zajednički rad. Kod terapijskog ugovora, klijent jasno definiše koje promjene želi da ostvari, i definiše šta je on spreman da uradi da pomogne realizaciji tih promjena. Terapeut definiše šta je on spreman da radi sa klijentom kako bi se postigle željene promjene.</li>
					<li>Otvorenost komunikacije. U toku terapijskog procesa i klijent i terapeut treba da imaju sve informacije o tome šta se dešava u njihovom zajedničkom radu. To proizilazi iz osnovne pretpostavke da su ljudi OK i da svako može da misli.</li>
				</ul>
					

				<h4>Ko ima koristi od terapije</h4>

				<p>Postoji jedna izreka u Transakcionoj analizi: „Ne moraš da se razboliš da bi ti bilo bolje.“ Ne morate da budete bespomoćni ili nesposobni da biste imali koristi od terapije. Možete biti i funkcionalna osoba koja će doći na terapiju zato što želi još više od života. Na terapiju može da krene svako ko ima ličnih problema, bilo da su u pitanju privremene teškoće u vezi ili na poslu, ili ozbiljniji mentalni poremećaji.</p>

				<h4>Ciljevi promjene u Transakcionoj analizi</h4>

				<p>TA smatra da su disfunkcionalna ponašanja rezultat samoograničavajućih odluka koje je osoba donijela rano u djetinjstvu kako bi preživjela. Skup  tih odluka naziva se životni scenarij (skript) i on putem nesvjesnih svakodnevnih odluka određuje kakav će život osoba živjeti (kao gubitnik, kao pobjednik ili dosadno – bez većih razočarenja, ali i uspjeha).</p>
				<p>Cilj transakcione analize je napustiti skript, te ponovno uspostaviti <b>autonomiju</b> ličnosti kako bi osoba mogla svjesno donositi ispravne odluke. </p>
				<p>Erik Bern je smatrao da je autonomija ideal kome treba težiti i ona se ostvaruje uspostavljanjem tri sposobnosti: svjesnosti, spontanosti i intimnosti. Svjesnost je sposobnost da vidimo, čujemo, osjetimo, okusimo i omirišemo stvari kao čiste senzualne impresije (kao novorođena beba) bez tumačenja i filtriranja. Spontanost je sposobnost da odaberemo između niza mogućnosti za osjećanje, mišljenje i ponašanje i da reagujemo slobodno iz bilo kojeg od svoja tri ego stanja. Intimnost podrazumjeva otvoreno dijeljenje osjećanja i želja između vas i druge osobe. Proces postizanja autonomije može se predstaviti sledećom pjesmom:</p>
