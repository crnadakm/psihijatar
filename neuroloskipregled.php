				<h4 id="par1">Neurološki pregled - temeljna procjena stanja zdravlja našeg živčanog sistema</h4>
<p>Neurološki pregled namijenjen je uspostavi dijagnoze stanja kao što su delirijum, Parkinsonova bolest, multipla skleroza (MS), spinalna mišićna atrofija ali i kod glavobolje, nesanice, zaboravnosti te mnogih drugih. </p>
<p>Neurološki pregled - korak po korak obuhvata sljedeće dijelove</p>
<p>Kognitivni pregled - procjena mentalnog statusa pacijenta,  svjesnosti i orijentisanosti pacijenta  na (1) osobu, (2) mjesto i (3) vrijeme. Neurolog će provjeriti da li osoba zna svoje  ime, da li može da identifikuje datum i da li razumije zašto je na pregledu. Ako pacijent neadekvatno odgovara na takva pitanja i djeluje zbunjeno ili dezorijentisano, možete se nastaviti dalje sa ispitivanjem kroz upitnik koji treba da  procijeni različite aspekte kognitivne funkcije, uključujući orijentaciju, pamćenje i jezik ( moguće je da neurolog zatraži konsultaciju psihijatra ili psihologa) </p>
<p>Testiranje motornih funkcija - odnosi se na procjenu pokreta, snage i tonusa mišića.  Neurolog započne tako što će primijetiti da li osoba imate bilo kakve nevoljne pokrete koji bi mogli da signaliziraju neurološke bolesti, poput tikova ili tremora. Takođe će pogledati da li postoje bilo kakve promjene u veličini ili izgledu mišića, kao što je atrofija (skupljanje) ili kontrakcije (mišići izgledaju kao da su zamrznuti na mestu)a zatim provjeriti tonus mišića tako što će osoubu zamoliti opusti tijelo, a zatim nježno pomjerati njene ruke i noge. Neka neurološka stanja utiču na tonus mišića. </p>
<p>I poslednji dio motoričkog pregleda je mjera snage. U ovom dijelu pregleda osoba će uložiti punu snagu za svaki pokret koji ljekar traži da učini. Svaka grupa mišića, kao što su bicepsi i tricepsi ruku ili mišići stopala, biće ocijenjena na skali od nula do pet.</p>
<p>Senzorno ispitivanje - neurolog će provjeriti osjećaj za oštar i lagan dodir, temperaturu, položaj i vibracije u sva četiri ekstremiteta, a možda i na drugim dijelovima tijela. Od osobe se zatraži da navede kada i gdje osjeeć različite senzacije, a ponekad se može tražiti da uporedi osjećaj na desnoj i lijevoj strani tijela. Lokacija senzornih promena pomaže da se tačno utvrdi koji nervi ili koji dio kičme je zahvaćen neurološkom bolešću.</p>
<p>Refleksi, hodanje  i kordinacija pokreta -  Postoji nekoliko refleksa koje će ljekar provjerava - refleksi odgovaraju određenim živcima koji izlaze iz kičmene moždine, a refleksne promjene mogu pomoći llekaru da sazna da li imate bolest u određenim dijelovima mozga, kičmene moždine ili nerava.</p>
<p>Neurolog će takođe provjeravati  koordinaciju i hod osobe/pacijenta. Pregledom hoda se dobija uvid u, ravnotežu, vid, snagu mišića i osjećaj za prostor. </p>
<p>Koordinacija je povezana sa mnogim vještinama, a nedostatak u koordinaciji (koji će kroz niz testova provjeriti neurolog) može ukazati na uzrok neuroloških problema.</p>
<p>Jezik - neurolog ispituje da li osoba tečno govori i kako razumije govor i ukoliko nešto nije u redu to može ukazati da postoji jezički deficit koji se može i ne mora razviti usljed moždanog udara, tumora na mozgu, traume glave ili usljed nekog drugog uzroka</p>
