<p>Mentalno zdravlje je integralan i bitan sastavni dio zdravlja. U Statutu Svjetske zdravstvene organizacije stoji: „Zdravlje je stanje potpune fizičke, mentalne i socijalne dobrobiti, a ne samo odsutnost bolesti ili nesigurnosti”. Važna implikacija ove definicije je da je mentalno zdravlje više od odsustva mentalnih poremećaja ili hendikepa. <br>	
Mentalno zdravlje je stanje blagostanja u kojem pojedinac ostvaruje svoje sposobnosti, može se nositi s normalnim životnim stresom, može raditi produktivno i može dati svoj doprinos svojoj zajednici.<br>	
Mentalno zdravlje je ključno za našu kolektivnu i individualnu sposobnost da razmišljamo, osjećamo, komuniciramo jedni s drugima, zarađujemo za život i uživamo u životu. Na temelju toga, promocija, zaštita i obnova mentalnog zdravlja može se smatrati vitalnom brigom pojedinaca, zajednica i društava širom svijeta.</p>
<br><h3 id="par1">Odrednice mentalnog zdravlja</h3><br>
<p>Višestruki društveni, psihološki i biološki faktori određuju nivo mentalnog zdravlja osobe u svakom trenutku. Na primjer, nasilje i trajni društveno-ekonomski pritisci prepoznati su rizici za mentalno zdravlje.<br>
Loše mentalno zdravlje povezuje se s brzim socijalnim promjenama, stresnim uslovima rada, polnom diskriminacijom, seksualnim nasiljem, socijalnom isključenošću, nezdravim načinom života, fizičkim oboljenjima i kršenjem ljudskih prava.<br>
Neki psihički faktori ili/i faktori ličnosti čine ljude ranjivim na mentalne zdravstvene probleme, a biološki rizici uključuju genetsko naslijeđe.</p>
<br><h3 id="par2">Promocija i zaštita mentalnog zdravlja</h3><br>
<p>Promocija mentalnog zdravlja uključuje akcije koje poboljšavaju psihološku dobrobit. To uključuje stvaranje okruženja koje poštuje i štiti temeljna građanska, politička, socioekonomska te kulturna prava i ključno je za mentalno zdravlje. Bez sigurnosti i slobode ostvarenja tih prava teško je održati visok nivo mentalnog zdravlja.<br>
Nacionalne politike mentalnog zdravlja trebale bi biti usmjerene i na mentalne poremećaje i na šira pitanja koja promovišu mentalno zdravlje. Promovisanje mentalnog zdravlja trebalo bi uključiti u vladine i nevladine politike i programe. Osim zdravstvenog sektora, nužno je uključiti sektore obrazovanja, rada, pravosuđa, saobraćaja, ekologije, stanovanja i socijalne zaštite.<br>
Posebni načini promocije mentalnog zdravlja uključuju:
<br>● intervencije u ranom djetinjstvu (npr. osiguravanje stabilnog okruženja osjetljivog na zdravstvene i prehrambene potrebe djece, uz zaštitu od prijetnji, priliku za rano obrazovanje i interakcije koje su emocionalno podržavajuće i razvojno stimulativne);
<br>● podrška djeci (programi za učenje životnih vještina, programi za razvoj djece i mladih);
<br>● društveno-ekonomsko osnaživanje žena (npr. poboljšanje pristupa obrazovanju i mikrokreditnim razvojnim podrškama);
<br>● socijalnu podršku starijoj populaciji (inicijative za uspostavljanje novih prijatelajstava, društveni i dnevni centri za starije);
<br>● programi namijenjeni ranjivim populacionim grupama i pojedincima, uključujući manjine, migrante i osobe pogođene sukobima i katastrofama (npr. psihosocijalne intervencije nakon katastrofa);
<br>● promotivne aktivnosti mentalnog zdravlja u školama (npr. programi koji uključuju podržavajuće ekološke promjene u školama);
<br>● intervencije mentalnog zdravlja u poslovnim sistemima (npr. programi prevencije stresa);
<br>● politike stanovanja (npr. poboljšanje stanovanja);
<br>● programi prevencije nasilja (npr. smanjenje dostupnosti alkohola i pristup oružju);
<br>● programe razvoja zajednice (npr. integrisani ruralni razvoj);
<br>● smanjenje siromaštva i socijalna zaštita siromašnih;
<br>● zakone i kampanje protiv diskriminacije;
<br>● promociju prava, mogućnosti i brige o pojedinacima s mentalnim poremećajima.</p>
<br><h3 id="par3">Liječenje i zaštita osoba sa mentalnim poteškoćama</h3><br>
<p>U kontekstu nacionalnih napora za razvoj i provođenje politike mentalnog zdravlja, važno je ne samo zaštititi i promovisati mentalno blagostanje svojih građana, nego i brinuti se za potrebe osoba s definisanim mentalnim poremećajima.
Znanje o tome šta učiniti u pogledu eskalacije opterećenja mentalnim poremećajima znatno se poboljšalo u proteklim decenijama. Sve je veći broj dokaza koji pokazuju djelotvornost i isplativost ključnih intervencija za prioritetne mentalne poremećaje u zemljama na različitim nivoima razvoja. Primjeri ekonomsko prihvatljivih, izvedivih i pristupačnih intervencija uključuju:
<br>● liječenje depresije psihološkim liječenjem i, u umjerenim do teškim slučajevima, antidepresivima;
<br>● liječenje psihoze antipsihoticima i psihosocijalnom podrškom;
<br>● oporezivanje alkoholnih pića i ograničenje njihove dostupnosti i stavljanja na tržište.
<br><br>Postoji i niz drugih korisnih mjera za sprečavanje suicida, prevenciju i liječenje mentalnih poremećaja kod djece, prevenciju i liječenje demencije te liječenje poremećaja zavisnosti o psihoaktivnim supstancama. Akcioni program za poteškoće mentalnog zdravlja iznio je smjernice utemeljene na dokazima kako bi se omogućilo da se bolje upravljaj nizom prioritetnih aspekata mentalnog zdravlja.</p><br>
<h3 id="par4">WHO (Svjetska zdravstvena organizacija - SZO) - odgovor na potrebe</h3><br>
<p>SZO podržava vlade u cilju jačanja i promocije mentalnog zdravlja. SZO je uvažio dokaze za promociju mentalnog zdravlja i radi s vladama na širenju tih informacija i uključivanju djelotvornih strategija u politike i planove.
Svjetska zdravstvena skupština odobrila je 2013. godine „sveobuhvatni akcioni plan za mentalno zdravlje za razdoblje 2013. -2020.”. Plan je obaveza svih država članica SZO da poduzmu posebne mjere za poboljšanje mentalnog zdravlja i doprinesu ostvarenju niza globalnih ciljeva.

Opšti cilj Akcionog plana je promocija mentalnog blagostanja, sprečavanje mentalnih poremećaja, briga, unapređenje oporavka, promociju ljudskih prava i smanjenje smrtnosti, morbiditeta i invaliditeta za osobe s mentalnim poremećajima. <br>	
</p>
Usmjeren je na 4 ključna cilja:
<ul>	
	<li>●jačanje djelotvornog vođenja i upravljanja u oblasti mentalnog zdravlja;</li>
	<li>●pružanje sveobuhvatnih, integrisanih i odgovornih usluga zaštite mentalnog zdravlja i socijalne zaštite u okviru zajednice;</li>
	<li>●provođenje strategije promovisanja i prevencije u oblasti mentalnog zdravlja; i</li>
	<li>●jačanje informacionih sistema, naučnih dokaza i istraživanja za mentalno zdravlje</li>
</ul>

<br>
<p>		
Poseban naglasak je u Akcionom planu na zaštiti i promociji ljudskih prava, jačanju i osnaživanju civilnog društva i središnjeg mjesta zajednice u brizi o mentalnom zdravlju.<br>
Kako bi se postigli ovi ciljevi, Akcioni plan predlaže i zahtijeva jasne mjere za vlade, međunarodne partnere i WHO. Ministarstva zdravlja moraće preuzeti vodeću ulogu i sarađivati s njima te s međunarodnim i nacionalnim partnerima, uključujući civilno društvo, kako bi se proveo plan. Budući da ne postoji djelovanje koje odgovara svim zemljama, svaka će vlada morati da prilagodi Akcioni plan svojim specifičnim nacionalnim okolnostima.
</p>



	
