<h3 id="par1">Prvi sporazum glasi: koristiću svoje riječi tačno</h3><br>
<p>Riječi su dar kojim dobijamo stvaralačku moć. Njima kazujemo o čemu sanjamo, šta osjećamo i kakvi zapravo jesmo. Riječi su moćno ljudsko oruđe sa dvije različite oštrice kojim možemo rasturiti sve oko nas ili ostvariti najljepše snove. Riječi oslobađaju ili bacaju u ropstvo, riječi osvjetljavaju ili gase život. <br>
Tačno koristiti riječ znači prestati koristiti riječ protiv sebe. Ako zloupotrijebimo riječi možemo trovati svijet, dok tačnom upotrebom riječi možemo gajiti razumijevanje i ljubav. <br>
Ako se ražestimo i svoju riječ pošaljemo kao otrov, ako nekome kažemo da je glup, pokvaren ili ljigav izgleda kao da koristimo riječ protiv te osobe ali zapravo riječ se okreće protiv nas, jer je vrlo vjerovatno da ćemo biti omraženi od te osobe. Da li je to ono što želite da dobijete od drugih? Da li je dobro da budete omraženi?<br>
Ako ste uvrijedili nekog, očekujte da ćete biti vrijeđani. Ako ste grubi očekujte grubost. Ukoliko ste zahvalni, više će biti zahvalnosti prema vama. Ako vi volite, mnogo je vjerovatnije da ćete biti prihvaćeni i voljeni. <br>
Ako prihvatate i volite sebe, ljubavi će biti u riječima kojima se obraćate drugima a takav čin uvećava mogućnost sličnog odgovora. </p>

<br>
<h3 id="par2">Drugi sporazum glasi: prestajem događaje shvatati lično.</h3><br>
<p>Svaki čovjek živi u vlastitom umu, u vlastitom snovima, u potpuno drugačijem unutrašnjem svijetu. 
Ako ono što se događa oko nas shvatamo lično onda pretpostavljamo da drugi već sigurno znaju kako je u našem svijetu ili pokušavamo da nametnemo svoj svijet drugima.<br>
Ono što drugi ljudi rade nije zbog nas. Oni to rade zbog sebe. Ako vam kažu da ste divni, oni to ne govore zbog vas, jer su već svjesni da to odranije možete znati, nego jer žele da vas obavijeste da im se dopadate i da žele da ste uz njih. Ako propuste da primijete kako ste divni stvari su slične, ali se u tom slučaju žele odmaknuti, zato što se plaše ili zato što ne žele nekog poput vas pored sebe. Šta god ljudi rade, osjećaju, misle ili kažu, možete prestati to shvatati lično. <br>
Kada si dopustimo da vidimo ljude onakve kakvi jesu, nećemo biti povrijeđeni onim što oni govore ili rade. Čak i ako vas drugi lažu, to je u redu. Oni lažu zbog toga što se plaše da ćete otkriti kako nisu savršeni u onoj mjeri u kojoj očekuju da trebaju da budu u vašim očima. Nikada niste odgovorni za ponašanje drugih; odgovorni ste samo za sebe. <br>
Kada usvojite ovaj sporazum, da biste donosili odluke biće potrebno da osluškujete samo sebe a da biste vjerovali u sebe neće trebati da iščekujete ono što drugi rade ili govore. <br>
Kada navika da ne shvatate lično postane duboko ukorijenjena, bijes na druge će nestati. Zavist, tuga, strah će se iseliti iz vašeg života, 
a vi možete postojati otvorena srca, spremni da tražite ono što želite i drugima reći da ih volite bez straha da će vas ismijati ili odbaciti. </p>

