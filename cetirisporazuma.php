<h3 id="par3">Treći sporazum glasi: prestajem da pretpostavljam.</h3><br>
<p>Stvarajući pretpostavke o onome što drugi rade i misle, pri tom pogrešno shvatajući lično a zatim okrivljujući njih i šaljući riječima emocionalni otrov lako dođemo u situaciju da ni iz čega napravimo krupnu dramu. <br>
Problem u stvaranju pretpostavki je što vjerujemo da su one istinite.<br>
Pretpostavljamo da ljudi uz koje živimo znaju što mi mislimo, da svi vide život onako kako ga mi vidimo, da misle onako kako mi mislimo, osjećaju onako kako mi osjećamo i da im nije nužno reći što želimo ili hoćemo. Pretpostavljamo da će nešto učiniti jer nas poznaju, a ako ne učine ono što pretpostavljamo da trebaju da učine, doživimo se zanemarenim ili poniženim. Pretpostavke predstavljaju predvorje patnje. <br>
Način da sebe spriječimo u stvaranju pretpostavke jeste da postavljajući pitanja ostvarimo jasniju komunikaciju. Ako nešto ne razumijete, nađite u sebi hrabrosti da postavljate pitanja sve dok ne osigurate da vam je potpuno jasno. Kakvi god da su odgovori, kada ih jednom čujete, nećete stvarati pretpostavke jer ćete znati kako jeste.<br>
Dopustite sebi dovoljno hrabrosti da tražite ono što želite. Svako ima pravo da odgovori „da“ ili „ne“. Na isti način, svako ima pravo da vas pita, i imate pravo reći „da“ ili „ne“. <br>
Uz pomoć ovog sporazuma ostvarujete put ka ličnoj slobodi a životna snaga će slobodno teći kroz vas .</p>

<br>
<h3 id="par4">Četvrti sporazum glasi: učiniću ono što mogu.</h3><br>
<p>Pod bilo kojim okolnostima, učinite ono što u tom trenutku možete. To neće biti uvijek isto. Kada se ujutro probudite puni snage, ono što možete uraditi više je od onoga što možete kada ste umorni. Razlikovaće se ono što možete uraditi kada ste zdravi i kada ste bolesni, kada ste trijezni ili pijani, a svakako će zavisiti i od toga da li ste ljuti, ljubomorni, osjećate se sretno ili ste uznemireni.<br>
Ukoliko se trudite učiniti više od onoga što možete, trošićete više snage nego što je imate, a kada se previše naprežete, slabite svoje tijelo i radite protiv sebe. Ono što učinite vjerovatno se ipak neće činiti dovoljno dobro. Ako činite manje od onoga što možete, okrećete sebe samoosuđivanju, osjećaju krivice ili kajanju jer se sprječavate u ostvarenju svojih želja i očekivanja. Ako prihvatite da date od sebe koliko ste u tom trenutku spremni, nećete sebe kriviti, a ako sebe ne krivite nećete kažnjavati sami sebe ili patiti zbog osjećaja stida.<br>
Možete imati mnogo ideja u svojoj glavi, ali ono što mijenja stvari jeste činjenje. Ako budete činili onoliko koliko ste u tom trenutku spremni bićete dobri prema sebi a imaćete vidljive rezultate. Činite zbog toga što tako hoćete, a ne zbog toga što tako morate ili što pokušavate zadovoljiti druge. Ukoliko nešto radite zbog toga što ste prihvatili prinudu, nećete biti u stanju darovati od sebe i tada je bolje da to uopšte ne radite. <br>Činiti što možete sjajna je navika, u svemu što radite i osjećate. Čineći ono što možete njegovaćete mogućnost da darujete od sebe bližnjima i svom okruženju i naučiti da primite ono što vama drugi daruju, disaćete punim plućima.</p>






