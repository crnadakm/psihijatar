				<p>Porodica obuhvata emocionalni sistem od najmanje tri generacije živih srodnika koji su međusobno povezani krvnim, zakonskim i istorijskim vezama. Za razliku od drugih sistema, porodični je jedinstven po tome što se u njega ulazi rođenjem, usvojenjem i brakom, a izlazi samo smrću (možda ni tada, s obzirom da uticaj um’rlih članova na ostale često ostaje prisutan). Pripadnost porodici je data, ona se ne bira, osim, u izvjesnoj meri, kada je brak u pitanju.</p>
