				<p>U početnim fazama rada, terapeut proučava funkcionisanje porodice, njenu strukturu, uloge, funkcije, pravila, komunikacije, kao i odnos porodice sa širim sistemom, fazu životnog ciklusa u kojoj se porodica nalazi, kritične životne događaje i načine na koje porodica rješava probleme i prilagođava se novonastalim promjenama. Na osnovu eksploracije porodičnog sistema terapeut pravi određene hipoteze putem kojih usmerava svoj dalji rad. Akcenat se stavlja na pronalaženje snaga i resursa porodice i pronalaženje izuzetaka od pravila. Porodice često dolaze sa uvjerenjem da su iscrpile sve svoje kapacitete u nošenju sa određenom problematikom i da se nalaze u slijepoj ulici. Uloga terapeuta, između ostalog, se sastoji i u tome da ukaže na izuzetke-one situacije u kojima su se članovi porodice pokazali kompetentnim i uspjeli da se izbore sa problemom. Fokus je na sadašnjem momentu (na ,,ovde i sada“) i povezivanju sa prošlošću radi preventiranja budućih problema.</p>
