<p>Psihijatrija je medicinska grana usmjerena na prevenciju, dijagnostikovanje i liječenje mentalnih i emocionalnih poteškoća i poremećaja u ponašanju.</p>

<p>Psihijatar je doktor medicine/ljekar sa specijalizacijom koja mu omogućava da se bavi mentalnim
zdravljem i njegovim poremećajima. Psihijatri su kvalifikovani da procijene i mentalne i fizičke aspekte
psihičkih poteškoća, uključujući i ponašanja koja podrazumijevaju ovisnost.</p>

<p>Pomoć psihijatra ljudi traže iz mnogih razloga. Poteškoće mogu biti funkcionalne i uzrokovati da se
svakodnevni život doživljava iskrivljeno ili van „kontrole“. Kod drugih postoje dugotrajni osjećaji tuge,
beznađa, tjeskobe ili se problemi pojavljuju iznenada, poput „napada“ panike, zastrašujućih vidnih
halucinacija ili slušanja „glasova”, pomisli o samoubistvu...</p>

<br>
<h3 id="par2">Dijagnostički pristup </h3>
<p>
Najvažniji aspekt rada psihijatra je povjerljiv, obziran i detaljan razgovor s pacijentom a budući da su ljekari, psihijatri mogu naručiti ili obaviti čitav niz medicinskih, laboratorijskih i psiholoških testova koji, pomažu u stvaranju slike tjelesnog i psihičkog stanja pacijenta. <br>
Medicinsko obrazovanje i kliničko osposobljavanje omogućuju razumijevanje kompleksnog odnosa emocionalnih stanja ili psihičkih poteškoća te tjelesnih bolesti s genetikom i porodičnom anamnezom. Integrisanjem medicinskih i psiholoških podataka, postavlja se radna dijagnoza i planira rad s pacijentima na liječenju.<br>
Specifične dijagnoze temelje se na kriterijima i klasifikacijama MKB (medjunarodna klasifikacija oboljenja) te, iako rjedje, na kriterijumima DSM (američka klasifikacija oboljenja) koji sadrži opise, simptome i druge kriterije za dijagnostikovanje psihičkih poremećaja.
</p><br>

<h3 id="par3">Koje tretmane koriste psihijatri?</h3>
<p>
Psihijatri koriste u liječenju širok spektar psihoterapijskih modaliteta, pouzdanu paletu farmakoterapijskih mogućnosti, predlažu raznovrsne psihosocijalne intervencije zavisno o potrebama svakog pacijenta.
Psihoterapija, ponekad pojednostavljeno nazvana terapija razgovorom, podrazumijeva kompleksan i sadržajan odnos između terapeuta i pacijenta a može se koristiti za liječenje širokog spektra mentalnih poremećaja i emocionalnih poteškoća. Cilj psihoterapije je razumjeti i ublažiti onesposobljujuće ili uznemirujuće simptome kako bi osoba bolje funkcionisaala. Ovisno o zadatku, liječenje može trajati samo nekoliko susreta tokom deset ili petnaest dana ili može trajati tokom razdoblja od nekoliko godina. Psihoterapija se može obaviti pojedinačno, u paru, s porodicom ili u grupi.</p>
<p>
Postoje psihoterapije koje pomažu pacijentima da promijene način ponašanja ili razmišljanja, psihoterapije koje pomažu pacijentima da istraže uticaj dosadašnjih odnosa i iskustava na sadašnje ponašanje ali i mnoge druge psihoterapijske metode prilagođene da pomognu u rješavanju problema.
Psihijatri nakon obavljene temeljne procjene mogu propisati lijekove koji pomažu u liječenju mentalnih poteškoća na isti način kao i lijekove za liječenje visokog krvnog pritiska ili dijabetesa. Iako precizan mehanizam djelovanja psihijatrijskih lijekova nije u potpunosti razumljiv, oni mogu korisno korigovati biohemijsku signalizaciju i komunikaciju unutar mozga, što umanjuje simptome psihijatrijskih poremećaja. Pacijenti koji se dugotrajno liječe lijekovima povremeno će se sastajati sa svojim psihijatrom kako bi pratili pozitivno djelovanje lijeka i moguća neželjena dejstva.
Lijekovi koji se preporučuju u psihijatriji su podijeljeni u više grupa – anksiolitici, sedativi i hipnotici, antidepresivi, antipsihotici, stabilizatori raspoloženja, stimulansi a njihovim ispravnim i odmjerenim korištenjem se dobijaju važni pomaci u rješavanju poteškoća mentalnog zdravlja. 
Psihijatri često propisuju lijekove u kombinaciji sa psihoterapijom.
Postoje i drugi psihijatrijski tretmani kao što je elektrokonvulzivna terapija (EKT), duboka moždana stimulacija (DBS), neurostimulacija vagusnog živca (VNS) i transskranijalna magnetna stimulacija (TMS) koje se koriste za liječenje nekih mentalnih poremećaja ali su u našem okruženju manje dostupne.
</p>
