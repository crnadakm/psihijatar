<h3 id="par4">Psihijatrijska edukacija</h3>
<p>Da bi postala psihijatar osoba mora završiti medicinski fakuletet i imati dozvolu za obavljanje medicinske djelatnosti sa položenim državnim ispitom a zatim završiti četiri godine psihijatrijske specijalizacije. Specijalistički staž tokom edukacije je djelimično u bolnici koja radi s pacijentima sa širokim spektrom medicinskih oboljenja a uglavnom u zdravstvenim ustanovama za zaštitu i liječenje mentalnog zdravlja gdje provodi najmanje tri dodatne godine učeći dijagnostikovanje i liječenje stanja i oboljenja povezanih sa mentalnim zdravljem, uključujući različite oblike psihoterapije i korištenje psihijatrijskih lijekova i drugih terapija. 
Nakon završenog treninga, većina psihijatara polaže ispit pred komisijom koju određuje institucije javnog zdravlja. Neki psihijatri takođe završe dodatnu specijalističku obuku iz forenzičke (pravne) psihijatrije , psihijatrije zavisnosti, psihofarmakoterapije ili psihoterapije kao posebnu obuku iz psihijatrija za djecu i adolescente.</p>
<br>
<h3 id="par5">Gdje rade psihijatri?</h3>
<p>
Psihijatri rade u okruženjima samostalne prakse, opštim i psihijatrijskim bolnicama, univerzitetskim medicinskim centrima, pri sudovima i kazneno popravnim domovima, domovi za starije osobe, programima rehabilitacija, hitnim službama i na brojnim drugim mjestima. 
U RS i BiH postoji oko 350 psihijatara koji rade u kliničkim i bolničkim ustanovama, centrima za zaštitu
mentalnog zdravlja ili ordinacijama van sistema javnog zdravstva, mnogi od njih rade i u više okruženja.</p>

