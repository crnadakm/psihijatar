<p>Kada ove potrebe ili gladi nisu zadovoljene u djetinjstvu i dalje na formativnom uzastu, one ne nestaju. Negirane potrebe nastavljaju da nesvjesno utiču na mišljenje, osjećanje i ponašanje. Kao što biološke gladi zadovoljavamo hranom i vodom kako bismo se razvijali mentalno i tjelesno, tako zadovoljavamo psihološke gladi da bismo se razvili fizički i mentalno.</p>
<p>Tjelesna svjesnost, prepoznavanje osjećanja i potreba, svjesnost o drugačijim opcijama zadovoljenja potreba su značajne snage jedne ličnosti. Rad na gojaznosti može da bude dobar povod za razvoj vještina za kvalitetan život.</p>
<h4 id="par3">Cilj terapije gojaznosti</h4>
<p>Gubitak kilaže nije primaran cilj. Rad na uzroku je efikasniji od rada na simptomu. Važno je da otkriti zašto je klijentu potrebno da ima veliko tijelo ili da se prejeda. </p>
<p>Ključni zadatak je da osoba prihvati da ima izbore u vezi svoje težine. Pravi psihološki i socijalni izbor ide iz razumijevanja zašto je bilo potrebno prejedanje ili veliko tijelo jer ako se pokuša smršaviti samo pomoću dijeta ili vježbanja zbog osećaja gađenja prema sebi, krivice ili pritiska okoline, izbor tada ne ide iz integrisanog, cjelovitog bića. Tako se samo prigođavamo spoljnim zahtjevima što nije dobar osnov za stabilnu promjenu. Kada razvijemo osjećaj za samovrednovanje i samoljubav, iz te pozicije možemo da odlučimo da li ćemo mršaviti ili ne. Psihoterapijski cilj je autonomija (spontanost i sloboda izbora) i osnaženje i razvoj ličnosti.</p>
 
