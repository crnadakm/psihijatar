<p><b>Prejedanje i debljanje su rezultat riješenosti da preživimo!!!</b></p>
<p>Zvuči apsurdno da neko ko ima prekomjernu težinu vjeruje da je to jedini način da bude živ pored svih onih bolesti koje idu uporedo sa tim (dijebetes melitus, hipertenzija, teškoće sa kretanjem...) ali osoba uglavnom podsvjesno zaista vjeruje da neće preživeti ako nije gojazna. Takva odluka je odbrambena u situacijama kada, usled traumatskih iskustava ili deficita psiholoških ili socijalnih resursa, neki funkcionalniji mehanizmi nisu dostupni pa osoba na nekom nivou zaključi da je najbolje za nju da se prejeda i održava svoje tijelo velikim kako bi se lakše snašla u ovom svijetu. Osoba se, takođe, u isto vrijeme muči sa tim disfunkcionalnim načinom razriješavanja unutrašnje nelagodnosti i to postaje njen najveći neprijatelj.</p>
<p>Svako je rođen sa sposobnošću da zna kad je gladan i kad mu je dosta hrane. Odrasle gojazne osobe su izgubile zdravi osjećaj biološke gladi i jedu kad im ne treba hrana dok se ne prepune iako im treba nešto drugo (sigurnost, podrška, razumijevanje ili zadovoljenje neke od drugih psiholoških gladi). Razvijanjem svjesnosti o tjelesnim senzacijama mogu se povezati sa svojim tijelom i početi da razumijevaju razliku između potrebe za hranom i drugih potreba, da se pobrinu o sebi, da dobiju pažnju i reakciju od okoline. Svesnost o tjelesnim senzacijama, prepoznavanje i razlikovanje potreba, zatim, učenje načina kako se one zadovoljavaju su prvi koraci u radu na ovom problemu. </p>
