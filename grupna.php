	<h2>INFORMACIJE I SMERNICE ZA UČESTVOVANJE U GRUPNOJ TERAPIJI </h1>
		<br>

<p>Grupna terapija ima dugu, dokazanu istoriju kao veoma 
delotvoran i koristan oblik psihoterapije. Pomaže isto, a 
ponekad i više od individualne terapije, posebno kad su važni 
ciljevi lečenja socijalna podrška i učenje o interpersonalnim 
odnosima. Mnoge osobe, koje učestvuju u grupnoj terapiji, 
imaju velike koristi od nje. Iako grupna terapija uglavnom 
pruža veliku podršku, ponekad može biti stresna.</p>
<br>
<h3 id="par1">NEKI CILJEVI GRUPNE PSIHOTERAPIJE</h3>

<p>Mnogi pojednici kreću na terapiju jer se osećaju izolovano ili 
su nezadovoljni određenom životnom situacijom. Možda 
imaju teškoće da uspostave i održavaju prisne, uzajamno 
zadovoljavajuće i sadržajne odnose sa drugim ljudima. Često 
žele više da nauče o tome kako da se odnose prema drugima.</p>

<ul>
	<p>Grupna terapija ovim osobama pruža priliku da:</p>

	<li>• Prime i pružaju podršku i fidbek</li>
	<li>• Poboljšaju interpersonalne odnose i komunikaciju</li>
	<li>• Eksperimentišu sa novim interpersonalnim ponašanjima</li>
	<li>• Iskreno i direktno govore o osećanjima</li>
	<li>• Steknu uvid u svoje misli, osećanja i ponašanja i razumeju ih 
posmatrajući obrasce odnosa i unutar i van grupe</li>
	<li>• Razumeju misli, osećanja i ponašanja drugih ljudi</li>
	<li>• Poboljšaju samopouzdanje, sliku o sebi i samopoštovanje</li>
	<li>• Ostvare ličnu pramenu unutar grupe, uz očekivanje da će 
naučena preneti i na spoljašnji život</li>
</ul>
<br>
<h3 id="par2">POVERLJIVOST</h3>
<p>Svakom iskazu učesnika u psihoterapiji mora se prilaziti sa 
poštovanjemi poverljivošću. Ovo je suštinski deo etičkog i 
profesionalnog ponašanja.</p>

<p>a) Terapeutibr	<br>
Grupni terapeuti zaklinju se da neće prekršiti poverljivost, 
osim u jednom slučaju: kada postoji neposredna opasnost 
ozbiljne povrede člana grupe ili nekoga drugoga.
Ako ste na uporednoj individualnoj terapiji, tražimo vašu 
dozvolu da u redovnim intervalima razgovaramo sa vašim 
terapeutom. Terapeuti su vaši saveznici i za terapiju je važno 
da međusobno razgovaraju.</p>
<p>b) Članovi grupe <br>
Poverljivost se očekuje i od svih članova grupe. Da bi stvorili 
bezbedno okruženje za terapijski rad i razvili poverenje u 
grupi, članovi ne smeju da krše poverljivost.Većina osoba voli 
da terapija bude nešto samo njihovo i suzdržavaju se od 
razgovora o njoj sa drugim ljudima. Međutim, ako u 
razgovoru sa prijateljima ili porodicom u nekom trenutku 
poželite da pomenete svoju terapijsku grupu, treba da pričate 
samo o sopstvenom iskustvu, a ne o iskustvima drugih 
članova. Nikada ne pominjite imena članova ili bilo šta na 
osnovu čega bi mogli biti identifikovani.</p>
<p></p>
<br>

<h3 id="par3">ŠTA RADITE U GRUPI? KAKO TREBA DA SE PONAŠATE?</h3>
<p>Nema propisanog plana rada za svaku seansu. Učesnici se podstiču da govore o bilo kom ličnom problemu ili problemu u odnosima, u vezi sa problemima i ciljevima zbog kojih su na terapiji. Učesnici se podstiču da pružaju podršku, postavljaju pitanja, razmišljaju o stvarima koje su rečene, ili o onima koje nisu, da dele asocijacije i misli. Akcenat se, pre svega, stavlja na istraživanje odnosa između članova, to jest, na „ovde-i sada". Od članova će se često tražiti da dele utiske koje imaju jedni o drugima - misli, strahove i pozitivna osećanja. Što više radimo ovde- i-sada u grupi, to smo delotvorniji. </p>
<p>Samootkrivanje je neophodno da bi članovi imali koristi od grupne terapije, ali oni sami biraju trenutak. Nikada ne vršimo pritisak na članove da se ispovedaju. Da bi se uspostavilo terapijska grupno okruženje, zahtevamo od članova da uvek pokušavaju da ostalima govore stvari na konstruktivan način. Korisna povratna informacija usredsređena je na ono šta se dešava ovde-isada, ne okrivljuje, relevantna je i povezuje članove koji je primaju sa onima koji je pružaju. Ovakva vrsta direktnog fidbeka i angažovanja je novina: u našoj kulturi pojedinci retko pričaju tako iskreno i direktno. Otuda se ovo u početku može činiti rizičnim, ali takođe može biti i veoma angažujuće i smislena. Direktno davanje saveta od strane članova grupe i terapeuta uglavnom nije korisno. Nisu korisni ni opšti razgovori o temama kao što su sport ili politika, osim ako postoji nešto u tekućem događaju što je posebno važno za nečije lične i interpersonalne probleme. Terapijska grupa nije mesto na kom se stiču prijatelji. Ona je socijalna laboratorija -mesto na kom osoba usvaja veštine za razvijanje smislenih i zadovoljavajućih odnosa. U stvari, terapijske grupe (za razliku od grupa podrške ili socijalnih grupa) ne podstiču kontakt sa ostalim članovima van grupe. Zašto? Zato što spoljni odnos sa drugim članom ili članovima glavnom ometa terapiju! Kako se terapija ometa? Da bismo ovo objasnili, prvo moramo da naglasimo da je vaš osnovni zadatak u terapijskoj grupi da u potpunosti istražite svoje odnose sa svakim pojedinačnim članom grupe. U početku, ovo može izgledati čudno ili vam se može činiti da nije u vezi sa razlozima zbog kojih ste krenuli na terapiju. Ipak, ovo ima smisla kada se uzme u obzir činjenica da je grupa socijalni mikrokosmos - to jest, problemi koje doživljavate u društvenom životu pojaviće se i u vašim odnosima unutar grupe. Prema tome, istražujući i razumevajući sve aspekte svojih odnosa sa drugim članovima, a zatim prenoseći to znanje na svoj spoljašnji život, vi započinjete proces razvijanja zadovoljavajućih odnosa. Međutim, ako razvijete bliske odnose sa drugim članom (ili članovima) van grupe, možda nećete biti voljni da podelite sva svoja osećanja o odnosima u grupi. Zašto? Zato što vam to prijateljstvo može toliko značiti da ćete oklevati da kažete bilo šta Što bi ga, na bilo koji način, moglo ugroziti. Šta se dešava u terapijskoj grupi kada su otvorenost i iskrenost kompromitovane? Terapija se zaustavlja! Dakle, najbolje je da članovi koji se susreću van grupe (slučajno ili namerno) podele sve relevantne informacije sa grupom. Bilo kakva tajnovitost u vezi sa odnosom usporava terapiju. Ponekad 
članovi razviju snažna osećanja prema drugim članovima. Podstičemo razgovor o ovim osećanjima, bilo da su pozitivna ili negativna - iritacija ili razočaranje. Od članova grupe očekuje se da govore o osećanjima, a ne da delaju u skladu s njima.</p>
<br>

<h3 id="par4">GRUPNI TERAPEUTI</h3>
<p>Vaši grupni terapeuti neće „voditi program". Njihova uloga je 
više uloga učesnika/moderatora, nego uloga instruktora. 
Terapija je najproduktivnija kada počiva na saradnji. Imajte na 
umu da su informacije koje daju drugi članovi isto toliko 
važne koliko i komentari terapeuta, a nekad i važnije. 
Terapeuti mogu da pruže zapažanja o grupnoj interakciji i 
ponašanju ili o tome šta određene osobe kažu ili rade u grupi. 
Takođe mogu i da komentarišu napredak ili prepreke unutar 
grupe. Kada želite nešto da kažete grupnom terapeutu, 
nadamo se da ćete to učiniti na seansi. Međutim, ukoliko 
postoji nešto hitno o čemu morate da razgovarate sa grupnim 
terapeutima van grupe, između seansi, to se može 
organizovati. Ali korisno je na sledećem sastanku izneti grupi 
o čemu ste razgovarali sa terapeutima. Treba podeliti čak i 
važan materijal sa individualne terapije ili terapije parova sa 
drugim terapeutom. Nadamo se da neće biti pitanja o kojima 
ne možete da govorite unutar grupe. U isto vreme, svesni smo 
da se poverenje razvija vremenom i da ćete neke stvari o sebi 
otkriti tek pošto se budete osećali dovoljno bezbedno u grupi.</p>
<br>

<h3 id="par5">POČETNA DUŽINA PROBNOG PERIODA ILI POSVEĆENOSTI</h3>
<p>Grupna terapija uglavnom ne pokazuje odmah pozitivnu 
korist svojim učesnicima. Zbog ove činjenice učesnici ponekad 
požele da ranije napuste terapiju ako je postala suviše stresna 
za njih. Molimo vas da se uzdržite od preranih zaključaka o 
mogućoj korisnosti grupe i nastavite da dolazite ili 
razgovarate o stresu i svojim sumnjama u vezi sa grupnom 
terapijom. Molimo vas da ostanete posvećeni i učestvujete u 
terapijskoj grupi na najmanje dvanaest seansi. Do tada ćete 
imati jasniju sliku o potencijalnoj koristi grupe. </p>
<br>

<h3 id="par6">PRISUSTVO I GRUPNA KOHEZIJA</h3>
<p>Grupa najefikasnije radi ako je kohezivna, pouzdana i 
predvidljiva. Redovno prisustvo je ključni deo, pa vas molimo 
da vam terapija bude prioritet u rasporedu. Grupna terapija 
najbolje napreduje kada svaki član ceni i poštuje posvećenost i 
rad svakog drugog člana. Redovno prisustvo i aktivno učešće 
na sastancima važan je način da pokažete ovo poštovanje. 
Slično tome, važan je i dolazak na vreme. Ako znate da ćete 
kasniti ili da nećete prisustvovati sastanku, molimo vas da 
pozovete grupne terapeute što je pre moguće kako bi na 
početku seanse o tome mogli da obaveste grupu. Ako za 
kašnjenje ili odsustvo znate nedelju ili više dana unapred,
obavestite grupu na prethodnoj seansi. Takođe, molimo vas 
da grupu na vreme obavestite o svojim planovima za godišnji 
odmor. Grupni terapeuti će učiniti isto. Može biti trenutaka 
kada je grupa poslednje mesto na kojem želite da budete zbog 
neprijatnih osećanja. Ovi trenuci, u stvari, mogu da budu 
neobično produktivna prilika za psihoterapiju. Isto, možete 
očekivati da će se neke teškoće koje ste iskusili u životu 
pojaviti i u grupi. Nemojte da vas to obeshrabri. Ovo je, u 
stvari, odlična prilika jer znači da ste se vi i članovi grupe latili 
važnih pitanja koja vas se tiču.
Prihvatanjem da učestvujete u grupnoj terapiji, odlučili ste da 
započnete proces davanja i primanja podrške, kao i rada na 
potrebnim promenama u svom ličnom i interpersonalnom 
životu. Radujemo se prilici da zajedno sa vama radimo u ovoj 
grupi.</p>
