<p>Izraz "relationship burnout" (sagorijevanje u vezi) slobodnije bi se mogao prevesti "zamka duge veze" ili "zaglavljenost bliskog odnosa" se odnosi na osobe u značajnom ljubavnom ili drugom  odnosu koje postepeno razvijaju doživljaj iscrpljenosti, dosade, bezvoljnosti i pesimizma prema onom drugom ili odnosu koji postoji između njih. Iako se većinom koristi za opis zamora i zaglavljenosti osoba u ljubavnoj vezi takođe je izraz primjenjiv na prijateljstva, odnose s članovima porodice te odnose sa kolegama. Javlja se često i u zdravim vezama nakon perioda dobrog funkcionisanja i ako se ne obrati obostrana pažnja na sprečavanje razvoja ovakve zaglavljenosti može dovesti do ljutitosti, iznurivanja i raskida veze.</P>
<br/>
<p>Budući da je dinamika svake bliskosti drugačija i poteškoće u vezi su drugačije za svakoga, ali neke znakovi su uobičajeni:</P>
<p><b>Prebacivanje krivice</b> -  učestalo svađanje bez zaključaka koji vode rješenjima problema oko kojeg se svađa započela. Jedna osoba upire prstom u drugog zbog nečeg negativnog (u terapiji je to nazvano "igrom okrivljavanja"). Problemi u vezi se lako pripisuju drugom umjesto da se uzimaju u obzir objektivne okolnosti odnosa te razmisli i o vlastitoj odgovornosti za poteškoće koje se pojavljuju.</P>
<p><b>Izostanak zajedničkog planiranja budućnosti</b> – razgovore o budućnosti ne započinje ili izbjegava jedan ili oba partnera – bilo da je to nešto jednostavno kao kratko putovanje ili važno kao plan karijere  ukazuje na umor ili nemotivisanost da se preuzmu obaveze. </P>
<p><b>Smanjena komunikacija i kvalitet upotrebe zajedničkog vremena</b> - osobe rijetko razgovaraju ili potpuno prestaju s razgovorima, zanemaruju ili jasno izbjegavaju jedno drugo (često iz straha od svađe) i smanjuju fizičku bliskost.</P>
<p><b>Manjak motivacije za unaprjeđenje veze</b> – Na svakom odnosu je potreban rad i većina parova prolazi kroz teška vremena, mijenjajući način kako doživljavaju vezu i otkrivajući područja za poboljšanje. Međutim, osobe u zamci sagorijevanja doživljavaju kao pritisak ili postaju uznemirene kada  se odlučuju na nove korake u radu na odnosu (uključujući i psihoterapijski rad) što može pogoršati trenutne probleme.</P>
<p></P><br/>
<p>Budući da su  veze jedinstvene i raznolike, sagorijevanje u vezi može imati bilo koji  pokretač, iako većina veza ima neke od osnovnih uzroka:</P>
<p><b>Nezadovoljstvo</b> -  može biti jednostavan put u sagorijevanje. Veza koja je prije bila uzbudljiva, počinje bivati rutinirana i ustajala iako ne postoje krupni problemi koje par treba da riješi. </P>
<p><b>Periodi izraženih stresnih okolnosti</b> - kao što su poremećaji mentalnog zdravlja, duge selidbe, nezdravo radno okruženje ili iznenadna nezaposlenost uzrokuje produženi period opterećenja za oboje u paru su prečica za sagorijevanje u vezi. Česti razgovori ili briga o tim stresorima, dovode do toga da emocionalno zanemaruju jedno drugo ili im je smanjena motivacija da provedu kvalitetno vrijeme zajedno radeći zabavne stvari. Dostizanje ove tačke emocionalne iscrpljenosti često učini da se ljudi osjećaju zarobljenima unutar vezi.</P>
<p><b>Neusklađena energija davanja i primanja</b> - ako se jedan član veze osjeća kao da ulaže znatno više truda i energije od drugog, osjećaj zanemarivanja i ozlojeđenosti u vezi može rasti. Ako ova neravnopravnost potraje predugo, nagomilana neprijatna osjećanja mogu dovesti do nedostatka motivacije za dalji razvoj veze.</P>
<p></P><br/>
<p>Ne postoji najbolji način da se prevaziđe izgaranje u vezi; svaki par je drugačiji, sa snagama i stresorima koji su im jedinstveni.. Međutim, evo nekoliko prijedloga protiv sagorijevanja u vezi i njegovanje zdrave veze:</P>
<p><b>Djelovanje kao tim</b> - obnavljanjem ideje o vezi kao timskom radu – nijedan partner neće imati doživljaj da je sam u tome. Otvorenim razgovorom o sagorijevanju (bez obzira da li ga samo jedan ili oba partnera osjećaju) i ponovnom posvećenošću jedno drugome,  par je zakoračio na put rješavanja doživljaja otuđivanja.</P>
<p><b>Izgovaranje očekivanja</b> - Kada jedan učesnik u odnosu ne zadovoljava svoje potrebe (bilo da su te potrebe emocionalne ili fizičke), važno je izraziti probleme. Ovo uspostavlja konkretna područja za poboljšanje, omogućavajući da se zajednički razvije planiranje odnosa.</P>
<p><b>Prepoznavanje okidača</b> - stresori, nesigurnosti, bolne tačke i okolnosti  dovode do zanemarivanja, izbjegavanja ili svađa. zajedničkim radom na prepoznavanju, rješavanju ili smanjenju ovih stresora, osjećaj sagorijevanja u vezi će se vjerovatno smanjiti.</P>
<p><b>Planiranje zabavnog zajedničkog vremena</b> – umjesto formalnog (kao obaveza ili posao) Uključivanjem aktivnosti u kojima je par nekada uživao zajedno i redovno odvajanje vremena za aktivnosti ili izlaske (jednom sedmično je odlična polazna tačka). Iako isprva može bude teško ili nezgodno, vrijedi nastaviti. Poštovanje vremena koje se provodi zajedno će učiniti da se osjećate prirodnije i s manje brige.</P>
<p><b>Poštovanje vremena drugog za samoću</b> - umjesto suviška zajedničkog vremena, spriječiće doživljaj zarobljenosti ili ugušenosti navikama i hirovima drugog. Otvoren razgovor sa partnerom o svojim potrebama i planiranje vremena za sebe i samoopuštanje će u krajnjoj instanci omogućiti da se osjećate osvježeno i lakše ponovo zbližavate.</P>
<p><b>Njegovanje fizičke intimnosti</b> - npr. ako sjedite daleko jedno od drugog dok gledate film, razmislite o držanju za ruke i milovanju, grlite se dok ste zajedno ili ako nemate seksualni kontakt, iskreno porazgovarajte sa svojim partnerom o tome. Smanjena fizička intimnost između partnera čest je uzrok zaglavljenosti u odnosu. To potiče anksioznost ili nedostatak motivacije za investiranje u vezi , pa je zdravo potražiti načine za postizanje boljeg fizičkog kontakta (rješenjima poput terapije za parove ili romantičnim vikendom sa partnerom).</P>
<p><b>Razgovor sa profesionalcem</b> - partnerskim ili porodičnim psihoterapeutom može biti od koristi. Ovi stručnjaci mogu procijeniti jedinstvene stresore i pokretače loših odnosa, ponuditi savjete i olakšati zdraviju komunikaciju. Oni također mogu pomoći partnerima da identifikuju znakove depresije, anksioznosti ili drugih problema sa mentalnim zdravljem koji doprinose lošijim odnosima i uputiti vas kako da ih riješite.</P>
<p><i>Budite strpljivi jer sagorijevanje u vezi nije nešto što možete savladati preko noći.</i> Čak i oni koji rade na svojoj vezi mogu se naći lako u pripisivanju krivice ili izbjegavanju komunikacije. Nastojte da budete otvoreni i iskreni u vezi sa svojim osjećanjima i nemojte se obeshrabriti kada se stvari u odnosu ne poprave odmah. Za napredak će biti potrebno vrijeme i trud oba partnera zajedno</P>

