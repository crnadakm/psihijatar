<h4 id="par2">
	Kako prepoznati depresiju?
</h4>Bolest je određena znacima  i simptomima koji ukazuju da se radi o depresiji 
<br/><br/>Dogovorom je utvrđen broj pokazate</h4>lja potrebnih za kliničku dijagnozu depresije, ali kombinacija i njihov tačan broj kod svake osobe različit jer depresija ne pogađa sve ljude na isti način. 
<br/><br/>Dakle ako veći dio dana postoji neki od sljedećih simptoma, i da je tako u većini dana a tokom dvije nedjelje ili duže, vjerovatno se srećete sa depresijom.  
<br/><br/>
<br/>•  gubitak interesa ili zadovoljstva za aktivnosti koje su nekad prijale
<br/>•  prisutno je turobno raspoloženje ili osjećanje tuge tokom većeg dana (ili razdražljivost kod djece i adolescenata) 
<br/>•  mršavljenje ili dobijanje na težini bez jasnog objašnjenja, ili značajno smanjenje ili povećanje apetita 
<br/>•  nesanica ili previše spavanja 
<br/>•  umor ili doživljaj manjka životne energije 
<br/>•  nemirni ili usporeni pokreti, govor i misli 
<br/>•  doživljaj bezvrijednosti ili ekstremne krivice 
<br/>•  otežano razmišljanje, ili donošenje odluka, manjak koncentrisanosti 
<br/>•  ponavljane misli o smrti ili samoubistvu,  
<br/><br/>Osim ovoga može doći do novih ili učestalih tjelesnih tegoba, bolova ili stomačnih problema, disajnih poteškoća ili poteškoća sa krvnim pritiskom. Nerijetko se mentalni problem mogu više iskusiti fizički više nego psihički. Primjetne promjene u ponašanju ili povećana upotreba psihoaktivnih supstanci takođe mogu biti znaci pokušaja samo-liječenja mentalnih tegoba.
<br/><br/>
<h4 id="par3">
	Depresija kod muških osoba
</h4>Muškarci sa depresijom će verovatnije češće od žena prijaviti specifične simptome 
<br/>•  Bijesa 
<br/>•  Agresivnosti, impulsivnosti
<br/>•  Zloupotrebu droga i alkohola 
<br/>•  Rizična ponašanja (vožnja, tuče ili drugi načini ugrožavanja sebe i drugih)
<br/><br/>Podržavajući socijalne norme, muškarci sa depresijom će možda prije ispoljiti određene nezdrave mehanizme suočavanja, kao što su radoholizam (pretjeran rad) ili kockanje. Manje je vjerovatno da će muškarcima nego ženama biti uspostavljena dijagnoza depresije. 
<h4 id="par4">
	Depresija kod ženskih osoba
</h4>Vrlo je vjerovatno da će žene sa depresijom prijaviti sledeće simptome: 
<br/>•  neodlučnost 
<br/>•  tjeskoba 
<br/>•  malodušnost 
<br/>•  poteškoće sa spavanjem 
<br/>•  stres
<br/>•  turobno raspoloženje 
<br/><br/>Depresija se kod žena dijagnostikuje dvostruko češće nego kod muškaraca.
<br/><br/>
<h4 id="par5">
	Depresija kod djece i mladih osoba
</h4>Depresija se često pojavljuje kod mladih između 12 i 17 godina, a stopa pojavljivanja kod mladih brzo raste. Prema podacima iz 2016. god. barem svaki deseta mlada osoba ima depresivne poteškoće. Nažalost, vjerovatno je da porast broja depresivnih mladih osoba ne utiče na proporcionalan porast onih koji zatraže pomoć koja im je potrebna. Dodatno zabrinjavaju rezultati objavljeni u načnim časopisima 2021. godine koji ukazuju da su se stope depresije i anksioznosti među djecom i tinejdžerima udvostručile tokom prve godine pandemije COVID-19 u poređenju sa onima prije pandemije. Pretpostavlja se da se 25% mladih bori sa simptomima depresije, a 20% ima simptome anksioznosti.
<br/><br/>Mlade osobe imaju mnoge iste simptome depresije kao i odrasli, ali te promjene raspoloženja i ponašanja ponekad se pomiješaju sa očekivanim ponašanjem u adolescenciji. 
<br/><br/>Dodatni pokazatelji depresivnosti kod mladih osoba mogu da uključuju: 
<br/>•  rastuženost i plačljivost bez jasnog razloga 
<br/>•  bijes ili frustracija, čak i zbog sitnih tema
<br/>•  gubitak interesovanja za aktivnosti u kojima su nekada uživali 
<br/>•  razdražljivost, osetljivost na odbijanje
<br/>•  krivica, posramljenost ili samokritika 
<br/>•  povlačenje iz društva, sukobi sa prijateljima i porodicom
<br/>•  neobjašnjive tjelesne tegobe i bolovi 
<br/>•  ispadi ljutitosti ispadi ili druga impulsivna ponašanja 
<br/>•  loše ocjene u školi ili bježanje iz škole 
<br/>•  samopovređivanje, misli o smrti ili samoubistvu
<br/><br/>
<h4 id="par6">
Komplikacije uz depresiju
</h4>Doživljavanje i preživljavanje epizode velike depresije često znači povećanu sklonost da se ponove epizode u budućnosti. Prema studiji iz medicinske psihologije, više od 13% ljudi koji se oporave od prve epizode velike depresije ima drugu u roku od pet godina; 23% u roku od 10 godina; i 42% u roku od 20 godina.
<br/><br/>
<br/><br/>Depresivnost negativno utiče i na druge aspekte zdravlja, uključujući i fizičko, socijalno i emocionalno blagostanje. Može dovesti do problema u ličnim odnosima i poslovnom životu. Depresija je povezana i sa rizikom od razvoja srčanih bolesti ili gojaznosti, anksioznih poremećaja, zloupotrebom alkohola ili droga, kao i kraćim trajanjem života koje povezano sa drugim medicinskim stanjima. 
<br/><br/>Depresija je takođe faktor rizika za samoubistvo. Više od devedeset procenata svih ljudi koji izvrše samoubistvo (10. vodeći uzrok smrti u SAD) ima u vrijeme smrti dijagnostikovanu psihijatrijsku bolest iz grupe depresivnih poremećaja prema Američkoj fondaciji za prevenciju samoubistva.
<br/><br/>
<h4 id="par7">
Kako dijagnostikovati depresiju?
</h4>Ljekar ili psiholog će takođe postavljati detaljna pitanja o raspoloženju, ponašanju, razmišljanju osobe kao i druge značajne podatke o okolnostima u kojima osoba živi a takodje i zatražiti od vas da uradite neke testove ili upitnike
<br/><br/>Prije dijagnostikovanja velike depresije, ljekar obavlja preglede i testove kako bi isključio druge probleme koji mogu da izazovu simptome, kao što su problemi sa štitastom žlijezdom, neželjena dejstva lijekova, neurološke bolesti, autoimune bolesti ili nutritivni nedostaci. Ovi pregledi mogu da uključuju fizički pregled i analize krvi, kao i diskusiju o  lijekovima koje osoba već koristi, od kojih neki mogu da izazovu depresivne simptome. 
<br/><br/>Iako postoji niz onlajn alata i testova koji mogu pomoći da se utvrdi da li je osoba depresivna i da li treba da potraži pomoć, ipak samo ljekar ili stručnjak za mentalno zdravlje može da dijagnostikuje kliničku depresiju. 
