Depresija je klinička bolest u kojoj neprijatne emocije traju nedjeljama ili duže. Često se uspostavlja kao psihijatrijska dijagnoza, i tvrdi se da pogađa 280 miliona ljudi širom svijeta, iako je za vjerovati da ima daleko više osoba koje pate ali se nisu nikada obratile za pomoć ili nisu prepoznate kao depresivne.
<br/><br/>Depresija je lječiva, a korisno je znati znake i simptome bolesti kako bi se što ranije pristupilo liječenju
<br/><br/>Iako muškarci, žene ili mlađe osobe mogu patiti na sličan način, razlike u polu i godinama ipak mogu da utiču na spoljašnje znake depresije kao i simptome pa je smisleno obratiti pažnju i na specifičnosti ove bolesti kod različitih populacionih grupa. 
<br/><br/><i>Znakovi oboljenja uopšteno su ono što u medicini ljekar može objektivno mjeriti i uočiti. To su npr. povišena temperatura, ubrzani puls, visok tlak, otvorena rana…
<br/><br/>Simptomisu ono što pacijent osjeća i primjećuje. Simptome ne može osjetiti neko drugi osim osobe koja ih ima. To su npr. mučnina, vrtoglavica, bezvoljnost, umor… </i>
<br/><br/>(razlika izmedju ova dva pojma često je zbunjujuća pa smtramo da vrijedi pojasniti)
