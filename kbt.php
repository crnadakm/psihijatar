				<h4 id="par1">Kognitivno bihejvioralna psihoterapija - ACT</h4>
<p>Kognitivno-bihejvioralna psihoterapija predstavlja kombinaciju tehnika i metoda kognitivne i bihejvioralne terapije. Pod bihejvioralnom terapijom podrazumijevamo niz različitih postupaka koji imaju za cilj usvajanje poželjnih ili uklanjanje nepoželjnih oblika ponašanja. Sa druge strane, osnovna ideja kognitivne terapije je da su emocionalne reakcije i ponašanje ljudi pod uticajem kognicija (misli, vjerovanja i interpretacija o sebi, drugima ili o situaciji) - „Nije važno šta nam se događa već kako to procjenjujemo“.</p>
<p>Cilj kognitivno-bihejvioralne psihoterapije, kao psihoterapijskog pristupa vođenog naučnim istraživanjima, je da izmjenom disfunkcionalnih misli, sprječavanjem patološkog ponašanja ili učenjem novog, adaptibilnog ponašanja redukujemo patološke sadržaje i poboljšamo kvalitet života klijenta. KBT je najprije usmjeren na istraživanje, razumijevanje i objašnjavanje faktora koji u sadašnjosti utiču na stvaranje i održavanje psiholoških problema, jer se ljudi ponekad previše zadrže na sjećanjima iz prošlosti i zaborave da žive sad i ovdje. Pomoću ove terapije ljudi uče kako da se fokusiraju na sadašnji trenutak i žive punom svjesnošću, s obzirom na to da je, prema naučnim istraživanjima, prebacivanje pažnje na sadašnji trenutak uspješan pristup za širok raspon problema.</p>
<p>Razvoj KBT-a je tekao kroz tri talasa:</p>
<ol>
	<li>Bihejvioralne terapije zasnovane na klasičnom i operantnom uslovljavanju</li>
	<li>Kognitivne terapije zasnovane na mijenjanju kognicija i socijalnom učenju</li>
	<li>Terapije zasnovane na prihvatanju i punoj svjesnosti.</li>
</ol>
<p>U okviru trećeg talasa KBT-a izdvajamo terapiju prihvatanjem i posvećenošću (eng. Acceptance and Commitment Therapy - skraćeno ACT) koja predstavlja inovativnu terapiju ponašnja. ACT je terapija kreirana sa ciljem da pomogne pojedincima da smanje nepotrebnu patnju i da krenu naprijed sa građenjem onakvog života kakvog smatraju vrijednim življenja.</p>
<p>Prema ACT modelu, problematična ponašanja svih ljudskih bića zasnovana su na istim procesima koji se odvijaju u pozadini tih ponašanja. Zbog ove pretpostavke, u terapiji prihvatanjem i posvećenošću, klijent i terapeut se nalaze na istom nivou u terapijskom odnosu, gdje terapeut klijentu zapravo pruža drugačiju perspektivu problema sa kojim se klijent suočava.</p>
<p>Model na kojem je zasnovan ACT usmjeren je na šest primarnih oblasti:</p>
<ol>
	<li>Prihvatanje</li>
	<li>Defuziranje</li>
	<li>Kontakt sa sadašnjim trenutkom</li>
	<li>Self kao kontekst</li>
	<li>Vrijednosti </li>
	<li>Posvećeno djelovanje</li>
</ol>
<p>Na početku ACT terapije klijent i terapeut zajedno istražuju koji su to faktori iz okoline koji utiču na ponašanje klijenta. Istraživanje se zatim usmjerava na otkrivanje načina na koji se neefikasno ponašanje pojačava ili osnažuje, a na koji način efikasno ponašanje slabi u životu klijenta, tako da život te osobe ne sadrži stvari koje on ili ona vrednuje. Iz perspektive ACT-a, postoji nekoliko potencijalnih procesa koji doprinose neefikasnom ponašanju ljudi, a koji su zajednički za sva ljudska bića. Dva najznačajnija procesa su izbjegavanje doživljaja i kognitivna fuzija. Izbjegavanje doživljaja se može opisati kao nevoljnost da se dožive sopstvene misli, osjećanja, sjećanja i ostali unutrašnji događaji. Kako bi izbjegli unutrašnje događaje ljudi koriste različite strategije, pomoću kojih će promijeniti učestalost ili doživljaj sopstvenih misli, emocija, sjećanja ili tjelesnih senzacija, a koje su za njih neugodne ili odmažuće. Upotreba ovih strategija kao rezultat donosi ponašanja (pretjerano spavanje, društvene mreže, alkoholizam, emocionalno prejedanje) u kojima je izbjegavanje ključni proces za razvoj i održavanje različitih psiholoških poremećaja. Sa druge strane, kognitivna fuzija odnosi se na strogo i kruto praćenje misli i pravila u svojoj glavi kod klijenata (npr. „Više ne mogu da podnesem ovu anskioznost“). Ljudi često vjeruju u sve misli koje su proizvod njihovog uma. Neke od tih misli jesu korisne, ali ima i onih koje nisu. Problem nastaje kada ljudi slijepo vjeruju u te odmažuće misli, koje nerijetko mogu biti rezultat negativnih ranijih iskustava u interpersonalnim odnosima. Umjesto toga, klijenti mogu tražiti načine da što efikasnije stupaju u interakciju sa neposredno doživljenim svijetom, umjesto onog verbalno konstruisanog u „umu“ osobe.</p>
<p>U okviru ovog psihoterapijskog tretmana klijent i terapeut rade na povećanju psihološke fleksibilnosti, a time i na otvorenosti za doživljaj različitih unutrašnjih događaja, kako bi klijent mogao sam donositi odluke o tome šta želi napraviti sa unutrašnjim događajima koji su za njega neprijatni. Usmjerenost na unutrašnju kontrolu može da bude sama po sebi dio problema zbog kojeg se klijent javio na terapiju. Kroz ACT klijenti uče da zapravo smanjivanje kontrole nad unutrašnjim doživljajima dovodi do oslobođanja za ostvarivanje veće kontrole nad svojim životom. Na povećanju psihološke fleksibilnosti i poboljšanju života klijenta radi se pomoću šest ranije navedenih procesa - prihvatanje, defuziranje, kontakt sa sadašnjim trenutkom, self kao kontekst, vrijednosti, posvećeno djelovanje. Na ovaj način ljudi dobijaju više prostora za rad na stvarima koje su za njih bitne, čime se zapravo kreću ka životu koji je za njih vrijedan življenja.</p>

<h4 id="par2">Terapija usmjerena saosjećanjem - CFT</h4>
<p>Terapija usmjerena saosjećanjem (engl. Compassion Focused Therapy, CFT) jeste terapija prema kojoj se pretpostavlja da je ljudima sa visokim nivoom stida i kriticizma teško dostupan sistem emocionalne regulacije koji stoji u osnovi ličnog doživljaja umirenja, sigurnosti i dobrobiti. Drugim riječima, prema ovoj terapiji, ljudi koji imaju izražen stid i samokritiku imaju velike poteškoće da osjete olakšanje i doživljaj umirenja i sigurnosti. Suprotno od ovog sistema, njihove reakcije na unutrašnji i spoljašnji svijet su pod uticajem sistema za emocionalno reagovanje na prijetnju. Zbog toga je u okviru ove terapije najveći fokus na razvijanju drugačijeg, emocionalno toplijeg odnosa prema svojim iskustvima, ispunjenog sigurnošću i umirenošću, preko saosjećanja prema drugima i saosjećanja sa sobom.</p>
<p>Ova terapija polazi iz evolucione funkcionalne analize, na osnovu koje možemo izdvojiti tri glavna emocionalna sistema regulacije: sistem usmjeren na prijetnje i samozaštitu, sistem usmjeren na pokretanje, traženje i postignuće i sistem usmjeren na zadovoljstvo, umirenje i pripadanje.</p>
<p>Prvi sistem, odnosno sistem usmjeren na prijetnje i zaštitu je razvijen s ciljem da nas upozori i usmjeri nam pažnju na otkrivanje prijetnje i da pokrene reagovanje na nju. U okviru ovog sistema najviše se izdvajaju emocije ljutnje, anksioznosti i gađenja, a ponašanja koja su najčešća jesu borba, bijeg, pokoravanje i zaleđivanje.</p>
<p>Sistem usmjerena na resurse, potragu i postignuće odnosi se na sistem koji je zadužen za energiziranje i pokretanje ponašanja koja pomažu organizmu da primjeti i obrati pažnju na povoljne resurse, te da omogući doživljavanje zadovoljstva i pokrenutosti u pribavljanju tih resursa.</p>
<p>Treći sistem je odgovaran za odmaranje, jer omogućava stanje mirovanja i spokoja kada pojedinac više nije fokusiran na prijetnju ili na potragu i konzumiranje resursa.</p>
<p>Terapija usmjerena saosjećanjem započinje sa jednostavnim intervencijama koje su fokusirane na uvježbavanje stvaranja podržavajuće i pomažuće emocionalne teksture alternativnim mislima, novim pomažućim ponašanjima i izlaganju iskustvima s ciljem da se stvori topao glas, kao kada pričamo sa prijateljima, i da stvarno pokušamo da osjetimo njegov uticaj, odnosno s ciljem da klijent započne sa korištenjem sistema usmjerenog na afilijativnost, zadovoljstvo, umirenje i pripadanje. S obzirom na to da je prvi sistem povezan sa lučenjem adrenalina i kortizola (hormoni koji se luče u stresnim situacijama), a drugi sa lučenjem dopamina (hormon povezan sa situacijama u kojima osjećamo zadovoljstvo, „pozitivnu tremu“), neophodno je napraviti balans u organizmu pomoću sistema usmjerenog na afilijativnost, zadovoljstvo, umirenje i pripadanje, jer se aktiviranjem ovog sistema luči oksitocin („hormon sreće“), koji je povezan sa psihičkom i mentalnom dobrobiti osobe.</p>


