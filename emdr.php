<h3 id="par1">Šta je EMDR?</h3>
<h4>Um često može sebe izliječiti prirodno, kao i tijelo.</h4>

<p>Francine Shapiro razvila je psihoterapijsku metodu EMDR 1987. god. 
Metoda koristi prirodni proces pokretanja očiju (EM – eye movemant) 
kako bi se uspješno liječio stresni poremećaj poslije traumatskog 
doživljaja (PTSP). Od tada, EMDR se koristi za efikasno liječenje 
različitih problema mentalnog zdravlja.
<p>Slova DR označavaju pojmove desenzitacija i reprocesiranje koji se 
mogu prevesti kao umanjenje osjetljivosti (podražljivosti) i ponovna 
obrada podataka.</p> 
<br>

<h3 id="par2">Šta se događa kada osoba doživi traumu?</h3>
<p>Većinu vremena rutinski obrađujemo nove podatke i iskustva bez da 
postoji svjesnost o tome.
Kada se desi neki zastrašujući događaj poput saobraćajne nesreće ili 
postoji neprestana izloženost nevolji kao što je zanemarivanje u 
djetinjstvu, proces suočavanja i obrade podataka može postati 
preopterećen. Zbog preopterećenosti uznemirujuća iskustva ostanu 
neprocesirana ili “zaleđena” u mozgu . „Zaleđena“ sjećanja i osjećanja 
su sačuvana u mozgu (dio koji se naziva limbički sistem) u „sirovom“ 
emocionalnom obliku prije nego u obliku koji se umije izraziti riječima.
Traumatska sjećanja tako ostaju u izolovanoj mreži memorije koja je 
povezana sa emocijama i tjelesnim senzacijama, a koja je odvojena od 
dijela mozga gdje se koristimo jezikom (riječima) za čuvanje sjećanja. 
Traumatska sjećanja mogu biti stalno pobuđena kada osoba doživi
događaje koje prepoznaje kao slične teškom iskustvu kroz koje je
prošla.</p>
<p>Slika događaja je često zaboravljena, ali bolna osjećanja kao što su anksioznost, panika, ljutnja ili očaj stalno se doživljavaju u sadašnjosti. Sposobnost da osoba živi u sadašnjosti i uči iz novih iskustava može biti značajno umanjena. 
EMDR pomaže stvoriti veze između memorijskih dijelova i mreža mozga, omogućavajući da mozak na prirodan način ponovo obradi traumatsko sjećanje i objedini podatke na nov način.</p>

 <br>
<h3 id="par3">Kako izgleda EMDR seansa?</h3>
<p>Nakon temeljne procjene, biće Vam postavljena pitanja o određenom 
uznemirujućem sjećanju. 
Pokreti očiju biće stimulisani poretima terapeutove ruke preko vašeg 
vidnog polja. Ponekad se umjesto
koristi šipka pokretnih svjetala ili slušalica. Pokreti traju kratko vrijeme, 
a zatim će biti zaustavljeni. Od vas biti zatraženo da izvjestite o 
iskustvima koje ste imali tokom svakog od tih setova pokreta očiju. 
Iskustva tokom seanse mogu obuhvatati promjene u mislima, slikama i 
osjećajima.
Obično se događa da sa ponovljenim setovima pokreta očiju, sjećanje se 
mijenja na takav način da se gubi njegov bolni intenzitet i postaje 
neutralno sjećanje na događaj u prošlosti.
Druga povezana sjećanja mogu se takođe izliječiti u isto vrijeme. Ovo 
povezivanje srodnih
sjećanja može dovesti do brzog poboljšanja u različitim aspektima 
vašeg življenja.
EMDR može biti kratak usmjeren tretman ili dio dužeg 
psihoterapijskog programa.
EMDR sesije mogu trajati 60 do 90 minuta.</p>
<br>
<h3 id="par4">Za šta se EMDR može koristiti?</h3>

<p>Osim upotrebe za liječenje posttraumatskog stresnog poremećaja, 
EMDR se uspješno koristi za liječenje:</p>
<p>
• anksioznosti i paničnih 
napada
• depresije<br>
• stresa<br>
• fobija<br>
• problema sa spavanjem<br>
• komplikovanog žalovanja<br>
• zavisnosti<br>
• boli, fantomske boli uda<br>
• samopoštovanja <br>
• straha od javnog nastupa
</p>
<br>
<h3 id="par5">Može li svako imati koristi od EMDR?</h3>

<p>Proces je brz i uznemirujuća iskustva, ako se dogode, traju 
relativno kratko vrijeme. Ponekad nije odgovarajuće za 
svakoga iskusiti snažne osjećaje i uznemirujuće misli, koje se
javljaju tokom sesije.
ali EMDR može ubrzati terapiju rješavanjem uticaja vaših 
prošlih trauma i omogućiti da živite potpuno u sadašnjosti.</p>
<br>
<h3 id="par6">Da li osoba zadrži kontrolu? </h3>

<p>Tokom EMDR osoba zadržava kontrolu, potpuno budna i 
svjesna. Ovo nije oblik hipnoze
i proces se može zaustaviti u bilo kojem trenutku. Tokom 
seanse, terapeut će podržati i olakšati
vaše samoliječenje. Reprocesiranje (prerada) se obično 
doživljava kao nešto spontano, a nove veze i spoznaje nastaju 
prirodno, iznutra i većina ljudi doživljava EMDR kao korisnu i
osnažujuću terapiju.</p>
<br>
<h3 id="par7">Koji dokazi postoje da je EMDR uspješan tretman?</h3>

<p>Broj onih kojim je ovaj inovativni klinički tretman uspješno pomogao je milionski. Pouzdanost EMDR je utvrđena istraživanjima u najmanje devetnaest kontrolisanih naučnih studija što je čini jednom o istraživanijih metoda koja se koristi u liječenju traume, (detaljnije na www.emdr-europe.org i www.emdr.org)</p>

