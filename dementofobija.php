				<div class="col-md-8 tekst">
		</br>Strah od toga da će izgubiti razum je veoma čest kod osoba koje se jave u ordinaciju psihijatra a zasigurno i značajno češći kod onih koji se ipak ne usude doći na razgovor sa profesionalcima za zaštitu mentalnog zdravlja. Izraženost ovog neprijatnog osjećanja je različita a ponekad je intenzitet takav da osoba može sebe zakočiti strahom i biti nesposobna da funkcioniše u svakodnevnom životu. Strah od ludila ili psihičke bolesti poznat je i kao dementofobija (od grčkih riječi dementos, što znači „izgubiti razum” i phobos, što znači „strah”). Dobra vijest je da postoji nepisano pravilo da je “strah od ludila (psihotične mentalne bolesti)” skoro siguran pokazatelj da se ludilo neće dogoditi jer osobe koje reaguju neurotskim (fobičnim) odbranama rijetko u stvarnosti razvijaju psihotične načine reagovanja.
		</br>
		</br>Bez obzira da li je izražena zabluda da će “poluditi” ili postoji zabrinutost zbog prisustva drugih psihičkih poteškoća, osoba izbjegava društvene situacije plašeći se da će halucinirati,“izgubiti razum” i neće moći da kontroliše svoje ponašanje. Postoji uvjerenje osobe da će učiniti nešto užasno ili bezumno pa biti obilježena “kao psihijatrijski slučaj” ili “ F šifra”.
		</br>Vrijedi ponoviti da strah od pretpostavljenog ludila, kao i druge pretpostavke u životu ne postaje istina zbog toga što se tako pretpostavi. Mnogi mentalno zdravi ljudi se izraženo plaše ludila, na primjer ako se nekom od roditelja dijagnostikuje depresija, bipolarni afektivni poremećaj, šizofrenija ili druga mentalna bolest njihova djeca će se vjerojatno plašiti “ludila” u određenom nivou. Slično tome u blizini druge bliske osoba sa mentalnim poteškoćama, moguć je povišen strah od psihičke bolesti. Neprijatan doživljaj u prošlosti zbog kojeg je tražena stručna pomoć (bijes, nasilno ponašanje, tjeskoba, depresivnost, zaboravnost, halucinacije, “napad panike”, „živčani slom“, pokušaj samoubistva…) koji se pogrešno povezuje sa kasnijim neminovnim “ludilom” pojavljuje se kao preteča straha od ludila. Strah može indukovati i neko drugi (nasilni partner ili roditelj koji osobu poredi sa nekim rođakom koji ima psihičke poteškoće). Bliski susreti i interakcije sa ljudima koji imaju teže psihičke poteškoće ili bolesti kao i uopšteno intenzivni stresni ili traumatični događaji tokom života mogu prethoditi strahu osobe da će poluditi. Fobije su češće kod starijih nego kod djece, tinejdžera ili mladih a žene se suočavaju sa većim rizikom nego muškarci.
		</br>
		</br><h4 id="par1">Koji su simptomi dementofobije? </h4>
		</br>Za razliku od mnogih specifičnih fobija koje se fokusiraju na spoljni objekat ili situaciju, dementofobija je strah od unutrašnjeg mentalnog i emocionalnog stanja.  
		</br>Prisutni su uznemirenje i tjeskoba sa drhtanjem, bljedilom i znojenjem. Osoba je umorna i bezvoljna, izmijenjenog ponašanja ili odnosa prema drugim ljudima i događajima. Povišena je samokritičnost uz izraženu sumnju i iscrpljujuće preispitivanje ličnih procjena. Osoba ima oslabljeno pamćenje, izbjegava zadatake koji zahtijevaju pojačanu koncentraciju i uz sve to se izoluje od drugih, osamljuje.
		</br>Kognitivni obrasci u dementofobiji podrazumijevaju zabrinutost zbog simptoma disocijacije (izmijenjenog doživljaja stvarnosti, okoline ili sebe samih), strah od razvoja šizofrenije ili fiksacije na drugu mentalnu bolest za koju se plaše da će je razviti, zabrinutost zbog mogućeg „nervnog sloma“, usmjerenost na priče ljudi koji su imali psihotične epizode, strah da će „završiti“ u psihijatrijskoj bolnici. Razvijaju teorije kakav će život biti kada polude, porede se sa nekim ko je mentalno ili emocionalno nestabilan, uvjereni su da se neko šali na njihov račun ili nepristojno komentariše da su „ludi“ ili „psiho“.
		</br>Nezdrava ponašanja koja mogu da pogoršaju dementofobiju uključuju istraživanje mentalne bolesti ili simptoma na internetu, izraženo praćenje najmanjih promjena raspoloženja, ponašanja, mišljenja, izolacija od drugih ili povlačenje iz sistema podrške praćeno stalnim analiziranjem misli, osećanja i postupaka u kojima se traže znakovi ludila. Ponekad osoba pokušava da stalno nešto radi da bi izbjegla neprijatna osećanja i strahove.
		</br>Prisutna je anksioznost, problemi sa snom ili apetitom, stres i problemi sa samopoštovanjem, što smanjuje vjerovatnoću da se osoba osjeća ispunjeno i zadovoljno u svom životu. Sumnja u svoj zdrav razum i  dovođenje u pitanje povjerenja u sopstveno rasuđivanje kod donošenja osnovnih odluka takođe utiče na rad i lični život. Nesposobnost da završi zadatke, prati svoju životnu rutinu i normalno funkcioniše u svakodnevnom životu uz fokusiranje na neprijatne misli može zauzeti toliko mentalnog kapaciteta osobe, da malo preostane da se posveti stvarima koje su im potrebne i koje žele da rade.
		</br>
		</br><h4 id="par2">Kako prevazići dementofobiju</h4>
		</br>Pored traženja stručne pomoći od terapeuta, psihologa ili drugog zdravstvenog radnika, postoje načini za bavljenje rješenjem ovog problema kojima se u  velikoj mjeri mogu smanjiti ili čak razriješiti simptomi. 
		<ul>
		<li>	Meditacija ili mindfulness - smanjuju stres, anksioznost i pomažu da se ublaži zabrinutost. Praksa od 10-15 minuta dnevno upotrebom aplikacije za meditaciju ili mindfulnes može biti dobar početak</li>
		<li>	Uvažiti mogućnost da se teške, negativne misli i osećanja – ne patologiziraju kao 'simptomi' i da to mogu biti  pojave koje povremeno može doživjeti svako.</li>
		<li>	Umjesto da se mijenja dnevna rutina kao odgovor na strah od ludila, bolje je nastojati da se  osoba drži svog uobičajenog rasporeda. </li>
		<li>	Otvoreni razgovori sa ljudima kojima vjerujete mogu vas naučiti da su slična osjećanja i iskustva češća nego što mislite. Većina ljudi se suočila sa sopstvenim (iracionalnim) strahovima, a slušanje o njihovim iskustvima može pomoći umanjenju doživljaja usamljenosti.</li>
		<li>	Obnavljanje samopouzdanja je veoma važno. Korisna su pitanja „Šta bih uradio u ovoj situaciji da vjerujem sebi 100%?“ ili „Kad bih bio siguran da nisam lud, ja bih (neku) situaciju sagledao na ovakav način...“ </li>
		</ul>
		</br><h4 id="par3">Liječenje dementofobije</h4>
		</br>Strah od ludila važi za jednu od lakših fobija za liječenje. Prihvatiti postojanje fobije je preduslov da se preraste doživljaj straha. Osoba koja strahuje od ludila doživljava da „gubi kontrolu“ i misli da „nije u redu“ kada ne može da se nosi sa svojim emocijama. 
		</br>Psihoterapija je dugoročno efikasan način liječenja dementofobije. Vođena od strane terapeuta kroz povezivanje osjećanja i misli osoba se uči vještinama kako da se nosi sa svojom tjeskobom kada se pojavi. Psihoterapija ima za cilj da pomogne da postane manje neugodno sa emocijama i situacijama koje ih mogu pokrenuti.
		</br>U terapijskom radu se kroz pitanja koja istražuju i strahove koji su postojali prije pojave straha od ludila približavamo rješenju.  Moguće je primijeniti bihejvioralnu terapiju ili neku drugu psihoterapijsku metodu.Vrste terapije koje se mogu koristiti za lečenje dementofobije uključuju kognitivno bihejvioralnu terapiju (KBT), terapiju prihvatanja i posvećenosti (ACT), pristupe zasnovane na svesnosti kao i desenzibilizaciju i ponovnu obradu pokretima očiju (EMDR) te psihofarmakoterapijsko liječenje.
		</br>Terapija ekspozicijom je vrsta KBT-a koja izlaže klijente njihovim strahovima, a istovremeno ih uči opuštanju i vještinama suočavanja.  Ova terapijska metoda može biti dostupna u zavisnosti od toga gdje živite, je još jedan način za trajno uklanjanje dementofobije. U tretmanu se osoba postepeno izlaže strahu, bilo u stvarnom životu, bilo kroz slike i videozapise. Očekivani ishod je da ova izloženost pomaže ljudima da nadvladaju svoj strah. 
		</br>Tri su faze liječenja:
		<ul>
		<li>	postepeno izlaganje stvari ili situaciji koja je zabrinjavajuća dok se simptomi ne smire i vremenom nestanu.</li>
		<li>	desenzibilizacija – kada je anksioznost dovoljno niska prelazi se  na viši nivo izloženosti. Ako nije, izloženost se produžava dok se to ne dogodi.</li>
		<li>	vježbanje – ponavljanje situacije dok se ne ustabili ovaj drugi način reagovanja.</li>
		</ul>
		</br>Lijekovi mogu umanjiti simptome ove fobije, ali su neefikasni u liječenju uzroka straha. Koriste se se antidepresivi, anksiolitici i antipsihotici.
		</br>Pronalaženje iskusnog terapeuta koji je specijalizovan za lečenje anksioznosti je važno, ali je podjednako važno odabrati terapeuta koji ima pristup i stil koji vam odgovara. 
		</br>
		</br><h4 id="par4">Zaključak</h4>
		</br>Dementofobija je izraženi strah od ludila. Simptomi i faktori koji uzrokuju dementofobiju mogu biti različiti i prisutni kao dugotrajan ili cjeloživotni problem. Ljudi koji pate od ove fobije često imaju iscrpljujuće misli o svojoj  nerazumnosti, anksioznost i sumnju u sebe.  Ako se ne liječi, ova fobija se može pogoršati sve dok ne za osobu postane preteško da funkcioniše, donosi 
		</br>odluke i fokusira se na obaveze. Dobra vijest je da se specifične fobije veoma dobro mogu liječiti kratkoročnim terapijama kao što su terapija izloženosti ili KBT. 
		</br>Značajno je zatražiti pomoć jer su izolovanost i doživljaj bespomoćnosti veoma izraženi ukoliko se ne poduzme liječenje. Važno je znati da se efekat ovog straha na život značajno može umanjiti.




