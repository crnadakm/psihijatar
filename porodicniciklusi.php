<p>Život porodice se tokom vremena odvija kroz specifične faze razvoja koje se zovu životni ciklusi porodice. Svaka faza razvoja ima svoje zadatke i izazove koje porodica treba da riješi i zadovolji kako bi funkcionalno prešla u narednu fazu životnog ciklusa. Ti zadaci i izazovi se mogu nazvati i razvojne krize. </p>
<p>Da bi se prešlo u narednu fazu, pred porodicu se stavlja zahtjev za promjenom, prilagođavanjem i mijenjanjem strukture i organizacije. Mijenjaju se odnosi, pravila, uloge, preispituju se granice i uvjerenja.</p>
<p>Kada porodica zna u kom se životnom ciklusu nalazi, to nudi mogućnost da se svjesno uhvati u koštac sa problemima koji se javljaju, adekvatnije i lakše prevaziđe izazove i krize, a samim tim i da kvalitetnije živi.</p>
<p>Faze životnog ciklusa porodice su:
