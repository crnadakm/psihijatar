<h4>Početna porodica</h4>
<p>Ovaj stadijum podrazumijeva period od početka zajedničkog života partnera do rođenja prvog djeteta. U ovom period partneri uče da funkcionišu kao diada (dvojnost). To podrazumijeva uspostavljanje granica prema roditeljskim porodicama (roditeljima, braći, sestrama). Uče da žive u zajedništvu kroz uspostavljanje njima svojstvenog stila komunikacije, pravila, granica, sistema vrijednosti. Ukoliko partneri ne uspiju da se prilagode na zajedništvo, to može dovesti do krize koja se može razriješiti razilaženjem i razvodom, ili nastoje da je prevaziđu rođenjem djeteta što privremeno razriješava probleme i prenosi ih u narednu fazu.</p>
<h4>Porodica sa malim djetetom</h4>
<p>Ovaj stadijum obuhvata trudnoću, rađanje prvog djeteta i period do predškolskog uzrasta djeteta (3-5 godina). Uspostavljanje roditeljskih uloga podrazumijeva raspodjelu osnovne brige, poslova, zaduženja, učešće u procesu vaspitanja. Potrebno je izbalansirati profesionalne potrebe, kao i potrebe za privatnošću, odmorom i zabavom. Često je neophodno redefinisati granice sa porodicama porijekla, koje su takođe dobile nove uloge (baka, deda), kao i održati partnersku relaciju i bliskost. Glavni cilj tretmana porodica u ovoj životnoj fazi je da se odraslima pomogne da postanu efikasni u svojoj novoj ulozi roditelja, uz istovremeno njegovanje partnerskog odnosa i postavljanje jasnih granica sa drugim članovima porodice.</p>
<h4>Porodica sa predškolskim djetetom</h4>
<p>Ovaj period traje od 3-5 godine djeteta do polaska u školu. Mogu se pojaviti problem vezani za roditeljstvo, ali i aktiviranje bračnih problema neriješenih u prethodnim stadijumima. Problemi se najčešće prikazuju kao "problemi sa djetetom", u vidu poteškoća sa uspostavljanjem navika spavanja, ishrane, itd. U terapijskom procesu je neophodno razlučiti razvojne poteškoće od simptoma, kao i roditeljsku od bračne disfunkcionalnosti.</p>
<h4>Porodica sa školskim djetetom</h4>
<p>Ova faza traje od polaska djeteta u školu do njegovog puberteta. S obzirom da dijete više odsustvuje iz kuće, roditelji imaju više vremena da se posvete profesionalnim obavezama i svom partnerskom odnosu. Dijete započinje proces socijalizacije, u njegov život ulaze školski drugovi, učitelji… Stiče radne navike, uključuje se u vanškolske aktivnosti. Roditelji i dijete su pred iskušenjem povratne informacije koju dobijaju od strane svih koji participiraju u školskom sistemu. Ona se direktno može tumačiti kao vrednovanje djeteta, pa roditelji preispituju svoj vaspitni uticaj i udio u izgradnji djetetove ličnosti. Poteškoće koje se mogu pojaviti kod djeteta u ovom periodu (problemi sa učenjem, ponašanjem, školska fobija) mogu biti maska neriješenih roditeljskih ili partnerskih konflikata iz prethodnih faza.</p>
<h4>Porodica sa adolescentom</h4>
<p>To je period kada dijete uđe u pubertet i traje do odlaska djeteta iz kuće. Porodica ulazi u novi proces reorganizacije. Roditelji ne mogu više da zadrže potpuni autoritet i od njih se traži fleksibilnost vezano za promjene. Ponekad je teško uspostaviti odgovarajući balans kontrole i slobode. Granice prema spoljašnjem svijetu moraju postati propustljivije. </p>
<p>Adolescenti unose novine u porodicu, izazivaju njene vrijednosti, često testiraju granice autoriteta, traže promjenu, ali i podršku i sigurnost. Razvojno su rastrzani između težnje za odrastanjem i dečije potrebe za roditeljskom zaštitom i brigom. Emotivni naboj je ono što karakteriše porodične odnose. Adolescenti su izuzetno podložni razvoju simptoma i često su teškoće prolazne prirode i odražavaju borbu za prelaz u novo životno doba. Simptomi mogu da budu i znak drugih disfunkcija u porodici, kao što su problemi u braku roditelja. Centralni događaj u braku, u ovoj fazi, jeste kriza srednjeg životnog doba jednog ili oba roditelja, što rezultira preuređivanjem odnosa u braku ili čak razvodom.</p>
<h4>Porodica iz koje odlaze djeca</h4>
<p>Ovo je najduža faza u životnom ciklusu porodice i veoma složena po svojim brojnim zadacima. To je stadijum kada se djeca puštaju da odu iz roditeljske porodice. Odvajanje djece od roditelja je promjena koja je bolna i za jedne i za druge. Roditelji moraju da se izbore sa promjenom sopstvenog statusa i pripremaju se za pomjeranje na poziciju bake/dede. Suočavaju se i sa starošću i smrću svojih roditelja.</p>
<p>U nekim porodicama se ova faza doživljava kao novi period za uživanje i zadovoljavanje potreba koje ranije nisu mogle biti ispunjene, kretanje u nove oblasti: putovanja, hobi, novi poslovi. Kod drugih, ovaj proces stvara osjećanje praznine i ogromnog gubitka, a ponekad i depresije. </p>
<h4>Postroditeljska porodica</h4>
<p>To je faza kada su sva djeca napustila roditeljski dom i partneri postaju okrenuti jedan drugom. Za ovaj stadijum je karakterističan "sindrom praznog gnezda". Bračni par, ostavši bez djece, doživljava veliku prazninu i gubitak smisla življenja. Posle mnogo godina u kojima je preokupacija uglavnom bila odgajanje djece, bračni par je ponovo sam i suočen sa značajnim bilansima ("da li je to ono što smo od života htjeli i ono što smo postigli?").  Situacija je posebno teška kada dođe do smrti jednog partnera na ovom stadijumu ili kada je u pitanju jednoroditeljska porodica.</p>
<h4>Ostarela porodica</h4>
<p>Ovo je faza koja sa sobom nosi neminovnosti, kao što su odlazak u penziju, ekonomsko pitanje egzistencije, bolest, smrt jednog partnera, što može da dovede stare ljude u zavisnu poziciju. Ko će ih njegovati i u kojim uslovima su nova pitanja za porodicu. Težak zadatak za u ovom periodu se odnosi i na suočavanje sa bliskim krajem.</p>
