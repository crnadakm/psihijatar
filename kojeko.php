<p>Ako razmišljate o tome da se sretnete sa nekim ko bi mogao pomoći u unapređenju i očuvanju vašeg mentalnog zdravlja moguće je da ste zbunjeni zbog različitih pojmova koje možete čuti ili pročitati. </p>
<p>Psihijatar? Psiholog? Psihoterapeut?  Vjerujemo da je smisleno da pojasnimo.</p>


<h2 id="par1">Psihijatar</h2><br>
<p>je ljekar specijalista, dakle ima medicinsku diplomu, uspostavlja medicinsku dijagnozu, i za razliku od drugih profesionalaca koji se bave mentalnim zdravljem, može propisati psihijatrijske (psihotropne) lijekove a svakako i pomoći u razmijevanju drugih medicinskih stanja. </p>

<p>Uvodni razgovor/pregled obavlja u  namjeri da prikupi medicinske i opšte podatke o pacijentu (anamneza) koji mu služe kao osnova za uspostavljanje dijagnoze (imenovanje poremećaja ili oboljenja), i u skladu sa kojima dalje predlaže određenu vrstu farmakoterapije (lijeka) i mjesta gdje će se liječenje provoditi (kućno liječenje, liječenje u dnevnoj bolnici, zadržavanje na bolničkom odjeljenju).</p>
<p>Mnogi psihijatri za sebe kažu da vjeruju više pravilnoj upotrebi lijekova nego drugačijim metodama liječenja i uz saradnju sa njima nerijetko je korisno posjetiti i psihoterapeuta da bi se proces ozdravljenja upotpunio uz terapijske razgovore. Kada je to moguće bilo bi dobro ako ovi različito usmjereni stručnjaci blisko sarađuju. Osim toga možete potražiti psihijatre koji su spremni i edukovani da se bave i psihoterapijom, pa je sam proces nešto jednostavniji za pacijenta/klijenta.</p>

<h2 id="par2">Neurolog </h2>
<p>je ljekar specijalista koji se bavi strukturom, funkcijom i organskim bolestima nervnog sistema. Obučen je za otkrivanje i liječenje oboljenja ili funkcionalnih smetnji mozga, leđne moždine, čulnih organa, perifernih živaca i mišića.</p>

<h2 id="par3">Neuropsihijatar </h2>
<p>koji je ljekar koji svoju specijalističku obuku završio u vrijeme kada obuka za srodne specijalizacije psihijatrije i neurologije nije bila razdvojena u različite edukacije. U praksi se najčešće sreće da se neuropsihijatri, u skladu sa ličnim opredjeljenjem i potrebama ustanova u kojima rade, bave više psihijatrijom ili više neurologijom iako ima i onih koji zadržavaju oba smjera profesionalnog rada.</p>


<h2 id="par4">Psiholog </h2>
<p>je profesionalac u oblasti mentalnog zdravlja koji je diplomirao na filozofskom fakultetu, odsjek za psihologiju. Psiholozi mogu biti zaposleni u različitim oblastima: vaspitno-pedagoškim ustanovama (školama i vrtićima), u zdravstvenim ustanovama, radnim organizacijama, u oblasti sporta, naučno- istraživačkoj oblasti itd.</p>
<p>Psiholozi koji rade u zdravstvenom sektoru su stručni saradnici u timovima za zaštitu mentalnog zdravlja, ali i na mnogim drugim odjeljenjima (psihijatrija, pedijatrija, onkologija, palijativna njega, fizikalna rehabilitacija...). Bave se procjenom kognitivnog i afektivnog  funkcionisanja ličnosti primjenom različitih psiholoških testova i tehnika (psihodijagnostika), te sprovode psihološke tretmane (podrška u procesu bolničkog i ambulantnog liječenja pacijenata, psihološko savjetovanje) aktivno učestvujući u timskom radu, te imaju ulogu u kreiranju preventivnih programa od važnosti za javno zdravlje.</p>
<p>Razgovor kod psihologa zasniva se na strukturisano vođenom psihološkom intervjuu i primjeni različitih testova i skala za koje su psiholozi tokom školovanja obučeni.  Psiholozi pišu nalaz i mišljenje, ali ne propisuju lijekove.</p>
<p>Neki psiholozi su dodatna stručna znanja i vještine stekli kroz višegodišnju specijalizaciju iz medicinske/kliničke psihologije ili edukaciju iz različitih psihoterapijskih modaliteta što proširuje polje korisnog djelovanja u radu sa ljudima. </p>


<h2 id="par5">Socijalni radnik </h2>
<p>je profesionalac koji pruža usluge socijalne zaštite u bolnicama ili drugim institucijama. Neki socijalni radnici takođe praktikuju psihoterapiju. Njihovo obrazovanje je donekle slično obrazovanju psihologa ali obično su prilagođeniji razumijevanju pojedinca u njegovom okruženju a nisu obučeni za psihološko testiranje.</p>

<h2 id="par6">Psihoterapeut  </h2>
<p>Ovo je zajednički izraz za stručnjake koji obavljaju aktivnosti čiji je osnovni cilj pružanje pomoći uglavnom razgovorom sa osobama koje imaju psihičke smetnje, koje prolaze kroz neku vrstu životne krize, imaju različite emocionalne poteškoće, probleme u komunikaciji ili su zainteresovani da pospješe lični rast i razvoj, saznaju nešto više o sebi i poboljšaju kvalitet svog života. Nisu svi psiholozi i psihijatri ujedno i psihoterapeuti već samo oni koji su završili određene obuke. U zavisnosti od akademske diplome, psihoterapeut može biti psihijatar, psiholog, socijalni radnik, andragog ili defektolog koji su pored svog osnovnog obrazovanja obavili višegodišnju specijalističku edukaciju za neki od psihoterapijskih modaliteta. Rad se obavlja sa pojedincima, parovima, grupama ili porodicama. </p>

<p>U našoj zemlji ne postoji zvanično zanimanje (djelatnost) psihoterapeut, za razliku od zanimanja psiholog i psihijatar. </p>
<p><i>Psiholozi, psihijatri, psihoterapeuti ne čitaju misli drugim ljudima, nisu čarobnjaci sa natprirodnim vještinama i ne koriste kugle za proricanje sudbine već u svom radu koriste provjerene naučne tehnike i metode kojima pomažu ljudima da razumiju, riješe, prevaziđu i kontrolišu svoje psihičke smetnje i unaprijede svoje psihološko funkcionisanje.</i></p>

