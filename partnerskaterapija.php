<p>Bračna/partnerska terapija predstavlja psihološki tretman parova koji su u vezi ili braku. Osnovni cilj jeste pružiti pomoć parovima da unaprede svoj odnos, u smislu višeg stepena poverenja, intimnosti, zadovoljstva i bolje komunikacije. </p>
<p>Neki od najčešćih razloga nezadovoljstva u braku ili vezi su: svađe i konflikti (koji dovode do toga da se jedan ili oba partnera osećaju odbačenima i proizvode visok stepen frustracije jer se ne nalaze rešenja za date nesuglasice), odsustvo komunikacije, nedostatak bliskost i intimnosti, međusobno optuživanje, posesivnost (težnja da se partner kontroliše), prevara...</p>
<p>Važno je istaći da je odgovornost za probleme uvek zajednička, čak i onda kada to tako ne izgleda na prvi pogled, tako da je veoma važno u terapijskom procesu da svaki partner prihvati sopstvenu odgovornost za narušen odnos (ovo je i preduslov za promenu).</p>
<p>Na početku terapije se identifikuje problem i definiše zajednički cilj, koji je prihvatljiv i poželjan za obe strane  i koji predstavlja osnovni generator motivacije za rešavanje problema.</p>
<p>Partneri u vezi ili braku moraju da rade na ispunjavanju dve kategorije zadataka. Jednoj kategoriji pripadaju zadaci koji se tiču odnosa, kao što su: međusobne obaveze, briga (pažnja), komunikacija, konflikti/kompromisi, intimnost i bliskost. Drugoj kategoriji pripadaju spoljašnji zadaci odnosa i tiču se uspostavljanja i održavanja veza sa drugim pojedincima, prijateljima, članovima porodice, kolegama, itd... Ključ za dobar (funkcionalan) brak je uspostavljanje ravnoteže između ,,unutra” i ,,spolja”.</p>
<p>Ljudska bića prirodno traže ljubav i povezanost sa drugima. Ljubav nas održava i predstavlja ,,sigurnu bazu“ iz koje istražujemo svet oko nas. Emocije predstavljaju značajan faktor u odnosu među partnerima. U okviru terapije parova, partnerima se pomaže da prepoznaju negativan komunikacijsko-emotivni ciklus, a nakon toga i da prepoznaju i izraze svoje potrebe i strahove u vezi, kako bi se ostvario veći stepen zadovoljstva i intimnosti u odnosu. Terapija, između ostalog, ima za cilj menjanje obrazaca komunikacije koji dovode do zaglavljivanja u vezi i koji su u formi kritikovanja praćenog međusobnim udaljavanjem partnera.</p>
<p>Postoje faze u razvoju parova koje su izazovnije od drugih i u kojima se češće javljaju problemi u odnosu, a to su: početak zajedničkog života, prva godina braka, odlučivanje o mogućem proširenju porodice, izazovi sa zasnivanjem porodice, rođenje deteta, kao i prva godina nakon rođenja, smrt bliskih osoba i članova porodice, period donošenja važnih odluka (selidba, škola, zdravstvene odluke), parovi sa malom decom, parovi sa školskom decom, period adolescencije dece, faza u kojoj deca napuštaju roditeljski dom, odlazak u penziju, zdravstvene poteškoće partnera...</p>
<p>Bračna ili partnerska terapija može pomoći partnerima da donesu odluku da li će ostati zajedno ili će se razdvojiti, a može biti korisna i nakon što je donesena odluka o razdvajanju, posebno u procesu razvoda, koji je često ispunjen konfliktima i emocijama poput ljutnje, besa, razočarenja, što može imati značajne negativne posledice na decu.</p>
<p>Terapeut nije u ulozi nekoga ko sudi, traži krivca ili slično, već je tu da  inicira i održi saradnju, sasluša, razume i prihvati realnost svakog partnera, kao i da olakša i unapredi međusobnu komunikaciju i razumevanje, pomogne u definisanju ciljeva i strategija za njihovo postizanje.</p>

